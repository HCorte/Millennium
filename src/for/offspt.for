	SUBROUTINE OFFSPT(BETS,NUMROWS,OFFSET)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
C
	INTEGER*4 BETS(*)
	INTEGER*4 OFFSET
	INTEGER*4 NUMROWS
	INTEGER*4 CNVTAB(0:2)
	DATA CNVTAB/1,2,4/
	INTEGER*4 BYTE, INDEX, OFFSETX
C
	OFFSETX=OFFSET-1
	DO 100 INDEX=1,NUMROWS
	BYTE=MOD(OFFSETX,3)
	BETS(INDEX)=CNVTAB(BYTE)
	OFFSETX=OFFSETX/3
100	CONTINUE
	RETURN
	END
