	SUBROUTINE GETTAX(WINAMT,TAXAMT,NETAMT)
	IMPLICIT NONE
        INCLUDE 'INCLIB:SYSPARAM.DEF'
        INCLUDE 'INCLIB:SYSEXTRN.DEF'
        INCLUDE 'INCLIB:GLOBAL.DEF'
        INCLUDE 'INCLIB:CONCOM.DEF'
        INTEGER*4 WINAMT,TAXAMT,NETAMT
C
C
	TAXAMT=0
	NETAMT=WINAMT
	IF(TAXLVL.EQ.0) RETURN
	IF(WINAMT.LE.TAXLVL) RETURN
	TAXAMT=IDINT(DFLOAT(WINAMT)*CALPER(TAXRAT))
	NETAMT=WINAMT-TAXAMT
	RETURN
	END
