	SUBROUTINE CLRQ_M_UFO(FAB)
	IMPLICIT NONE
C
	INCLUDE	'INCLIB:SYSPARAM.DEF'
	INCLUDE	'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:DISKQIO.DEF'
C
	INCLUDE	'($SYSSRVNAM)'
	INCLUDE '($IODEF)'
C
	RECORD /FABDEF/ FAB
C
	INTEGER*4   STATUS
C
C	*** SET THE BIT TO ALLOW QIO
C
	STATUS = IEOR('FFFFFFFF'X, FAB$M_UFO)
	FAB.FAB$L_FOP= IAND (FAB.FAB$L_FOP, STATUS) ! CLEAR THE BIT
C
	  RETURN
	  END
