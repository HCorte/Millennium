	SUBROUTINE COPYRITX(CLRLU)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
C
	INTEGER*4     CLRLU         !Logical unit to Clear screen
	INTEGER*4     ST            !Error status
C
C CLEAR THE SCREEN
C
	CALL CLRSCR(CLRLU)
C
C DISPLAY GTECH COPYRIGHT NOTICE
C
	CALL COPYRITE
C
C WAIT 2 SECONDS
C
	CALL XWAIT(2,2,ST)
C
	RETURN
	END
