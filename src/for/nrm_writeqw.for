	SUBROUTINE WRITEQW(FDB, BLOCK, BUFFER, STATUS)
	IMPLICIT NONE
C
	INCLUDE	'INCLIB:SYSPARAM.DEF'
	INCLUDE	'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:DISKQIO.DEF'
C
	INCLUDE '($SYSSRVNAM)'
C
	INTEGER*4   FDB(7)
	INTEGER*4   BLOCK
	INTEGER*4   BUFFER(*)
	INTEGER*4   STATUS
C
	INTEGER*4   HOLD
	INTEGER*4   I4TEMP
	INTEGER*2   I2TEMP(2)
	EQUIVALENCE (I4TEMP,I2TEMP)
C
C
	CALL WRITEQWX(FDB, BLOCK, BUFFER, STATUS)
	IF(.NOT.STATUS)THEN
	  GOTO 9000
	ENDIF
C
C	I/O COUNT IS NOT FULLWORD ALIGNED...GET IT ANYWAY
C
	I4TEMP = FDB(FDB_IOSB)
	HOLD   = I2TEMP(2)
	I4TEMP = FDB(FDB_IOSB+1)
	I2TEMP(2) = I2TEMP(1)
	I2TEMP(1) = HOLD
C
	FDB(FDB_IOLEN) = I4TEMP
C
9000	CONTINUE
	RETURN
	END
