C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE LOGGAMPAS(GDRW,GIND,DSKREC,GAMREC)
	IMPLICIT NONE

	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'

	INCLUDE 'INCLIB:LPAREC.DEF'
	INCLUDE 'INCLIB:DPAREC.DEF'
C
C PARAMETERS
C
	INTEGER*4 DSKREC(*),GAMREC(*), GDRW, GIND
C
C LOCAL VARIABLES
C
	INTEGER*4 INDAUX, I4_IND
C
C CONVERT PASSIVE LOTTERY RECORD
C       
	IF(GIND.LT.1 .OR. GIND.GT.NUMPAS) RETURN
	IF(GDRW.LT.1 .OR. GDRW.GT.PAGEMI) RETURN

	CALL FASTMOV(GAMREC,LPAREC,LPARECLEN)
	CALL FASTMOV(DSKREC,DPAREC,DPALEN)

	LPASTS(GDRW,GIND)    = DPASTS
	LPAEMIS(GDRW,GIND)   = DPAEMIS
	LPANUMTCK(GDRW,GIND) = DPANUMTCK
	LPANUMSER(GDRW,GIND) = DPANUMSER
	LPAPRC(GDRW,GIND)    = DPAPRC
	LPAESD(GDRW,GIND)    = DPAESD
	LPATIM(GDRW,GIND)    = DPATIM
	LPACTM(GDRW,GIND)    = DPACTM
	LPAPLAN(GDRW,GIND)   = DPAPLAN
	LPAPRGCDC(GDRW,GIND) = DPAPRGCDC
	LPAREDAMT(GDRW,GIND) = DPAREDAMT
	LPADIV(GDRW,GIND)    = DPADIV
	LPAEMT(GDRW,GIND)    = DPAEMT
	LPAWSER(GDRW,GIND)   = DPAWSER

	DO  I4_IND = 1, PAGDIV
	    DO	INDAUX = 1, PAGNBR
		LPAWIN(INDAUX,I4_IND,GDRW,GIND) = DPAWIN(INDAUX,I4_IND)
	    ENDDO
	ENDDO

	DO  INDAUX = 1, PAGDIV
	    LPAWNUM(INDAUX,GDRW,GIND)   = DPAWNUM(INDAUX)
	    LPASHR(INDAUX,GDRW,GIND)    = DPASHR(INDAUX)
	    LPASHV(INDAUX,GDRW,GIND)    = DPASHV(INDAUX)
	    LPAPAD(INDAUX,GDRW,GIND)    = DPAPAD(INDAUX)
	    LPATYP(INDAUX,GDRW,GIND)    = DPATYP(INDAUX)
	    LPADIG(INDAUX,GDRW,GIND)    = DPADIG(INDAUX)
	    LPAIDNUM(INDAUX,GDRW,GIND)  = DPAIDNUM(INDAUX)
	    LPAPRG(INDAUX,GDRW,GIND)    = DPAPRG(INDAUX)
	ENDDO

	DO INDAUX = 1, PAGEDV
	    LPAEXSHR(INDAUX,GDRW,GIND)  = DPAEXSHR(INDAUX)
	    LPAEXSHV(INDAUX,GDRW,GIND)  = DPAEXSHV(INDAUX)
	    LPAEXPAD(INDAUX,GDRW,GIND)  = DPAEXPAD(INDAUX)
	ENDDO

	LPACHGCDC(GDRW,GIND)    = DPACHGCDC
	LPABSD(GDRW,GIND)       = DPABSD
	LPAPUP(GDRW,GIND)       = DPAPUP
	LPARETCNT(GDRW,GIND)    = DPARETCNT
	LPANOFFRA(GDRW,GIND)    = DPANOFFRA
	LPATODPAY(GDRW,GIND)    = DPATODPAY
	LPATOPAYAMT(GDRW,GIND)  = DPATOPAYAMT
	LPARETAFTAMT(GDRW,GIND) = DPARETAFTAMT
	LPAMAXDAYPAY(GDRW,GIND) = DPAMAXDAYPAY
	LPADRAW(GDRW,GIND)      = DPADRAW

        DO  I4_IND = 1, NUMTOT
	  DO INDAUX = 1, NUMFIN
            LPAFIN(I4_IND,INDAUX,GDRW,GIND) = DPAFIN(I4_IND,INDAUX)
	  ENDDO 	
	ENDDO

        DO  I4_IND = 1, NUMTOT
	  DO INDAUX = 1, PAGDIV+PAGEDV
            LPADIVPLN(I4_IND,INDAUX,GDRW,GIND) = DPADIVPLN(I4_IND,INDAUX)
	  ENDDO 	
	ENDDO
	
	DO INDAUX = 1, 6
            LPALITDRW(INDAUX,GDRW,GIND) = DPALITDRW(INDAUX)
	ENDDO 		

	CALL FASTMOV(LPAREC,GAMREC,LPARECLEN)

	RETURN
	END
