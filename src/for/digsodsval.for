C=======OPTIONS /CHECK=NOOVERFLOW/EXT
        SUBROUTINE DIGSODSVAL(TERMES,TRABUF)
        IMPLICIT NONE

        INCLUDE 'INCLIB:SYSPARAM.DEF'
        INCLUDE 'INCLIB:SYSEXTRN.DEF'
        INCLUDE 'INCLIB:GLOBAL.DEF'
        INCLUDE 'INCLIB:CONCOM.DEF'
        INCLUDE 'INCLIB:DESTRA.DEF'
        INCLUDE 'INCLIB:CHKSUMCM.DEF'

C DEBUG - START /*
        INCLUDE 'INCLIB:IGSDEBUG.DEF'
C DEBUG - END */

        BYTE        TERMES(*)


C       INITIALIZE SOME FIELDS TO -1 (UNDEFINED)
        TRABUF(TIGSV_PMOD) = IGS_PMUND
        TRABUF(TIGSV_FNIF) = IGS_VFNUND
C DEBUG - START /*
        IF(IGSDEBUG(IA_INIGS)) THEN
           CALL OPS('TIGSV_PMOD',TRABUF(TIGSV_PMOD),TRABUF(TIGSV_PMOD))
           CALL OPS('TIGSV_FNIF',TRABUF(TIGSV_FNIF),TRABUF(TIGSV_FNIF))
        ENDIF
C DEBUG - END */

C
C IGS VALIDATION TRANSACTION BODY
C
C       GAME INDEX (1 - Placard)                                    (12)
        TRABUF(TGAMIND) = ZEXT(TERMES(7))
C DEBUG - START /*
        IF(IGSDEBUG(IA_INIGS)) THEN
           CALL OPS('TGAMIND',TRABUF(TGAMIND),TRABUF(TGAMIND))
        ENDIF
C DEBUG - END */

C       VALIDATE GAME INDEX
        IF(TRABUF(TGAMIND) .EQ. 1) THEN !PLACARD

C          BET REFERENCE DATE YEAR                                  (32)
           TRABUF(TIGSV_WRDY) = ZEXT(TERMES(20))
C DEBUG - START /*
        IF(IGSDEBUG(IA_INIGS)) THEN
           CALL OPS('TIGSV_WRDY',TRABUF(TIGSV_WRDY),TRABUF(TIGSV_WRDY))
        ENDIF
C DEBUG - END */

C          BET REFERENCE DATE MONTH                                 (33)
           TRABUF(TIGSV_WRDM) = ZEXT(TERMES(21))
C DEBUG - START /*
        IF(IGSDEBUG(IA_INIGS)) THEN
           CALL OPS('TIGSV_WRDM',TRABUF(TIGSV_WRDM),TRABUF(TIGSV_WRDM))
        ENDIF
C DEBUG - END */

C          BET REFERENCE DATE DAY                                   (34)
           TRABUF(TIGSV_WRDD) = ZEXT(TERMES(22))
C DEBUG - START /*
        IF(IGSDEBUG(IA_INIGS)) THEN
           CALL OPS('TIGSV_WRDD',TRABUF(TIGSV_WRDD),TRABUF(TIGSV_WRDD))
        ENDIF
C DEBUG - END */

C          BET REFERENCE GAME                                       (35)
           TRABUF(TIGSV_WRGM) = ZEXT(TERMES(23))
C DEBUG - START /*
        IF(IGSDEBUG(IA_INIGS)) THEN
           CALL OPS('TIGSV_WRGM',TRABUF(TIGSV_WRGM),TRABUF(TIGSV_WRGM))
        ENDIF
C DEBUG - END */

C          BET REFERENCE SERIAL NUMBER (HIGH ONE BYTE)              (37)
           TRABUF(TIGSV_WRSH) = ZEXT(TERMES(24))
C DEBUG - START /*
        IF(IGSDEBUG(IA_INIGS)) THEN
           CALL OPS('TIGSV_WRSH',TRABUF(TIGSV_WRSH),TRABUF(TIGSV_WRSH))
        ENDIF
C DEBUG - END */

C          BET REFERENCE SERIAL NUMBER (LOW FOUR BYTES)             (36)
           CALL TERM_TO_HOST(TERMES(25), TRABUF(TIGSV_WRSL), 4)
C DEBUG - START /*
        IF(IGSDEBUG(IA_INIGS)) THEN
           CALL OPS('TIGSV_WRSL',TRABUF(TIGSV_WRSL),TRABUF(TIGSV_WRSL))
        ENDIF
C DEBUG - END */

C          BET REFERENCE CHECK DIGITS                               (38)
           CALL TERM_TO_HOST(TERMES(29), TRABUF(TIGSV_WRCD), 2)
C DEBUG - START /*
        IF(IGSDEBUG(IA_INIGS)) THEN
           CALL OPS('TIGSV_WRCD',TRABUF(TIGSV_WRCD),TRABUF(TIGSV_WRCD))
        ENDIF
C DEBUG - END */

C DEBUG - START /*
        IF(IGSDEBUG(IA_INIGS)) THEN
           CALL OPS('P(SUPVAL): ',P(SUPVAL),P(SUPVAL))
           TYPE*,'P(SUPVAL): ',P(SUPVAL)
        ENDIF
C DEBUG - END */

C DEBUG - START /*
        IF(IGSDEBUG(IA_INIGS)) THEN
           CALL OPS('P(IGSPPLA): ',P(IGSPPLA),P(IGSPPLA))
           TYPE*,'P(IGSPPLA): ',P(IGSPPLA)
        ENDIF
C DEBUG - END */

C DEBUG - START /*
        IF(IGSDEBUG(IA_INIGS)) THEN
           CALL OPS('P(IGSPVAL): ',P(IGSPVAL),P(IGSPVAL))
           TYPE*,'P(IGSPVAL): ',P(IGSPVAL)
        ENDIF
C DEBUG - END */

C DEBUG - START /*
        IF(IGSDEBUG(IA_INIGS)) THEN
           CALL OPS('P(IGSPGVAL): ',P(IGSPGVAL),P(IGSPGVAL))
           TYPE*,'P(IGSPGVAL): ',P(IGSPGVAL)
        ENDIF
C DEBUG - END */

C
C IF FUNCTION SUPRESSED THEN SET ERROR MESSAGE
C
           IF((P(SUPVAL) .NE. 0) .OR. P(IGSPPLA) .NE. 0
     *                           .OR. P(IGSPVAL) .NE. 0
     *                           .OR. IGSGAMFLG(P(IGSPGVAL),
     *                                          IGS_GAMNUM(TRABUF(TGAMTYP),
     *                                                     TRABUF(TGAMIND))))
     *     THEN
              TRABUF(TERR)  = SUPR
              TRABUF(TSTAT) = REJT
C DEBUG - START /*
        IF(IGSDEBUG(IA_INIGS)) THEN
           CALL OPS('FUNCTION SUPRESSED!!!',0,0)
           TYPE*,'FUNCTION SUPRESSED!!!'
        ENDIF
C DEBUG - END */
           ENDIF
        ELSE
           TRABUF(TSTAT) = REJT
           TRABUF(TERR)  = SYNT
           SYNTERRCOD=5
C DEBUG - START /*
        IF(IGSDEBUG(IA_INIGS)) THEN
           CALL OPS('INVALID GAME INDEX: ',TRABUF(TGAMIND),TRABUF(TGAMIND))
           TYPE*,'INVALID GAME INDEX: ',TRABUF(TGAMIND)
        ENDIF
C DEBUG - END */
        ENDIF


        RETURN
       END
