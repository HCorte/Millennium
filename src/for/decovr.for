C    SUBROUTINE DECOVR-DECREMENT LOTTO POOL ON CONBINATION BET
C
C    CALL SEQUENCE-CALL DECOVR(OFFSET)
C    IN-OFFSET=OFFSET OF SEQUENCE SEQUENCE INTO LOTTO POOL TABLE
C
C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE DECOVR(OFFSET,GAM)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
C
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:POOLLTO.DEF'
C
	INTEGER*4 OFFSET, OFF, GAM
C
10	CONTINUE
	DO 210, OFF=1,MAXOVR-1
	IF (LTOVR(2,OFF,GAM).EQ.0) GOTO 220   !MUST BE IN PREVIOUS OVR
	IF (LTOVR(1,OFF,GAM).EQ.OFFSET) GOTO 250 !FOUND
210	CONTINUE
	CALL FLUOVR(GAM)                 !FLUSH ALL OVERLAYS
	GOTO 10
C
220	CONTINUE
	LTOVR(1,OFF,GAM)=OFFSET
	LTLSTOVR(GAM)=OFF
250	CONTINUE
	LTOVR(2,OFF,GAM)=LTOVR(2,OFF,GAM)-LTPOOL_GAMLIMIT(GAM)-1
	IF (LTOVR(2,OFF,GAM).NE.0) RETURN
C
C     PUT LAST OVERFLOW ON THIS PLACE
C
	LTOVR(1,OFF,GAM)=LTOVR(1,LTLSTOVR(GAM),GAM)
	LTOVR(2,OFF,GAM)=LTOVR(2,LTLSTOVR(GAM),GAM)
	LTOVR(1,LTLSTOVR(GAM),GAM)=0
	LTOVR(2,LTLSTOVR(GAM),GAM)=0
	LTLSTOVR(GAM)=LTLSTOVR(GAM)-1
C
	RETURN
C
	END
