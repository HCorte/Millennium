	SUBROUTINE RECARY
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
	INCLUDE 'INCLIB:NBRCOM.DEF'
	INCLUDE 'INCLIB:DESTRA.DEF'
	INCLUDE 'INCLIB:DESLOG.DEF'
	INCLUDE 'INCLIB:PRMHSH.DEF'
	INCLUDE 'INCLIB:PRMLOG.DEF'
	INCLUDE 'INCLIB:GTNAMES.DEF'
C
	INTEGER*4  TUBSIZ
	PARAMETER (TUBSIZ=I4BUCSIZ*7)
C
	INTEGER*4 DUMMY, AMT, POOL, GIND, I, ST
	INTEGER*4 BUF(LREC*3),BIGBUF(TUBSIZ),COUNT(MAXIND)
	LOGICAL WARN
	DATA COUNT/MAXIND*0/
C
C OPEN DAILY GAME(S) CARRYOVER FILE
C
	WRITE(5,900) IAM()
	CALL FASTSET(0,NBRSAL,NUMTOT*NBGPOL*NUMNBR)
	CALL IOPEN(SFNAMES(1,DCF),3,LREC*2,LCDC,LSER*2-1,ST)
	IF(ST.NE.0) CALL FILERR(SFNAMES(1,DCF),1,ST,0)
	CALL ITUBSIZE(3,TUBSIZ)
C
C
10	CONTINUE
	CALL ISREAD(BUF,3,BIGBUF,ST)
	IF(ST.EQ.ERREND) THEN
	  CALL ICLOSE(3,BIGBUF,ST)
	  DO 20 I=1,NUMNBR
	  IF(NBRDRW(I).LT.1) GOTO 20
	  WRITE(5,902) IAM(),COUNT(I),GTNAMES(TNBR),I
20	  CONTINUE
	  CALL FASTMOV(NBRSAL,NBRASL,NUMTOT*NBGPOL*NUMNBR)
	  RETURN
	ENDIF
	IF(ST.NE.0) CALL FILERR(SFNAMES(1,DCF),2,ST,0)
C
C CONVERT FROM LOG TO INTERNAL TRANSACTION FORMAT
C
	CALL LOGTRA(TRABUF,BUF)
	IF(TRABUF(TTYP).NE.TWAG) GOTO 10
	IF(TRABUF(TGAMTYP).NE.TNBR) GOTO 10
	IF(TRABUF(TSTAT).EQ.VOID.OR.TRABUF(TSTAT).EQ.INCA) GOTO 10
C
C REAPPLY WAGER TO GAME AND LIABILITY TABLES
C
	GIND=TRABUF(TGAMIND)
	IF(TRABUF(TGAMTYP).EQ.TNBR) THEN
	  CALL UPLIAB(TRABUF,.TRUE.,DUMMY,WARN)
	  COUNT(GIND)=COUNT(GIND)+1
	  IF(TRABUF(TWNAND).EQ.0) THEN
	    IF(TRABUF(TWBEG).GT.NBRDRW(GIND)) GOTO 10
	    IF(TRABUF(TWEND).LT.NBRDRW(GIND)) GOTO 10
	  ELSE
	    IF(TRABUF(TWBEG).NE.NBRDRW(GIND).AND.
     *         TRABUF(TWEND).NE.NBRDRW(GIND)) GOTO 10
	  ENDIF
	  DO 100 I=0,TRABUF(TWNBET)-1
	  POOL=TRABUF(TWNPOL1+I*3)
	  AMT =TRABUF(TWNAMT1+I*3)
	  NBRSAL(TRACNT,POOL,GIND)=NBRSAL(TRACNT,POOL,GIND)+1
	  NBRSAL(DOLAMT,POOL,GIND)=NBRSAL(DOLAMT,POOL,GIND)+AMT
100	  CONTINUE
	ENDIF
	GOTO 10
C
C
900	FORMAT(1X,A18,' Reprocessing carryovers')
902	FORMAT(1X,A18,1X,I7,2X,A8,I1,' tickets processed')
	END
