	SUBROUTINE READL(FDB, BLOCK, BUFFER, STATUS, LOCKSTATUS)
	IMPLICIT NONE
C
	INCLUDE	'INCLIB:SYSPARAM.DEF'
	INCLUDE	'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:DISKIO.DEF'
C
	INTEGER*4   FDB(FDB_LENGTH)
	INTEGER*4   BLOCK
	INTEGER*4   BUFFER(*)
	INTEGER*4   STATUS
	INTEGER*4   LOCKSTATUS
C
	INTEGER*4   FOR$RAB
	INTEGER*4   LUN
	INTEGER*4   BEGBLK
C
C
	LUN    = FDB(FDB_LUN)
	BEGBLK = (BLOCK-1)*FDB(FDB_BLKSZ) + 2
C
	CALL READXX(%VAL(FOR$RAB(LUN)), FDB, BEGBLK, BUFFER, STATUS)
C
	LOCKSTATUS = 0
C
	RETURN
	END
