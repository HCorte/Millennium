C
C
C CHECK IF TICKET IS A BONUS DRAW WINNER
C
C
C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE CHKBON(TRABUF,AMTWON,PRVFLG)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
	INCLUDE 'INCLIB:NBRCOM.DEF'
	INCLUDE 'INCLIB:DESTRA.DEF'
	INTEGER*4 AMTWON, BOXNUM, TEMP, FLAG, POL, AMT, K, N, I, ST, GIND
	INTEGER*4 NUM,BON
	INTEGER*4 DIG(4)
	LOGICAL   PRVFLG
C
C
	GIND=TRABUF(TGAMIND)
	IF(TRABUF(TWNTYP).EQ.NB4TYP) GOTO 1000
	IF(TRABUF(TWNTYP).NE.NB3TYP) RETURN
C
C THREE DIGIT GAME
C
	DO 110 BON=1,NBRBDR(GIND)
	DO 110 K=0,TRABUF(TWNBET)-1
	NUM=TRABUF(TWNNUM1+K*3)
	AMT=TRABUF(TWNAMT1+K*3)/NBRPRC(GIND)
	POL=TRABUF(TWNPOL1+K*3)
	IF(POL.GT.TNB3ST) THEN
	  N=NUM
	  DO 10 I=1,3
	  DIG(I)=MOD(N,10)
	  N=N/10
10	  CONTINUE
20	  CONTINUE
	  FLAG=0
	  DO 30 I=2,3
	  IF(DIG(I).GE.DIG(I-1)) GOTO 30
	  TEMP=DIG(I)
	  DIG(I)=DIG(I-1)
	  DIG(I-1)=TEMP
	  FLAG=1
30	  CONTINUE
	  IF(FLAG.NE.0) GOTO 20
	  BOXNUM=DIG(3)+DIG(2)*10+DIG(1)*100
	ENDIF
C
C CHECK STRAIGHT BETS
C
	IF(POL.LE.TNB3ST) THEN
	  IF(NUM.EQ.NBRWIN(POL,BON+1,GIND))
     *       AMTWON=AMTWON+NBRPRZ(POL,2,GIND)*AMT
	  GOTO 100
	ENDIF
C
C CHECK BOX BETS
C
	IF(POL.EQ.TNB3B3.OR.POL.EQ.TNB3B6) THEN
	  IF(BOXNUM.EQ.NBRWIN(POL,BON+1,GIND))
     *       AMTWON=AMTWON+NBRPRZ(POL,2,GIND)*AMT
	  GOTO 100
	ENDIF
C
C CHECK COMBO BETS
C
	IF(POL.EQ.TNB3C6.OR.POL.EQ.TNB3C3) THEN
	  AMT=AMT/2
	  IF(BOXNUM.EQ.NBRWIN(POL-2,BON+1,GIND))
     *       AMTWON=AMTWON+NBRPRZ(POL-2,2,GIND)*AMT
	  IF(NUM.EQ.NBRWIN(TNB3ST,BON+1,GIND))
     *       AMTWON=AMTWON+NBRPRZ(TNB3ST,2,GIND)*AMT
	  GOTO 100
	ENDIF
C
C CHECK WHEEL BETS
C
	IF(POL.EQ.TNB3W6.OR.POL.EQ.TNB3W3) THEN
	  IF(POL.EQ.TNB3W3) AMT=AMT/3
	  IF(POL.EQ.TNB3W6) AMT=AMT/6
	  IF(BOXNUM.EQ.NBRWIN(POL,BON+1,GIND))
     *       AMTWON=AMTWON+NBRPRZ(POL,2,GIND)*AMT
	  GOTO 100
	ENDIF
100	CONTINUE
110	CONTINUE
	IF(AMTWON.GT.REDMAX(TRABUF(TGAM))) PRVFLG=.TRUE.
	RETURN
C
C FOUR DIGIT GAME
C
1000	CONTINUE
	DO 1110 BON=1,NBRBDR(GIND)
        DO 1110 K=0,TRABUF(TWNBET)-1
        NUM=TRABUF(TWNNUM1+K*3)
        AMT=TRABUF(TWNAMT1+K*3)/NBRPRC(GIND)
        POL=TRABUF(TWNPOL1+K*3)
        IF(POL.GT.TNB3ST) THEN
	  N=NUM
          DO 1010 I=1,4
          DIG(I)=MOD(N,10)
          N=N/10
1010      CONTINUE
1020      CONTINUE
          FLAG=0
          DO 1030 I=2,4
          IF(DIG(I).GE.DIG(I-1)) GOTO 1030
          TEMP=DIG(I)
          DIG(I)=DIG(I-1)
          DIG(I-1)=TEMP
          FLAG=1
1030      CONTINUE
          IF(FLAG.NE.0) GOTO 1020
          BOXNUM=DIG(4)+DIG(3)*10+DIG(2)*100+DIG(1)*1000
        ENDIF
C
C CHECK STRAIGHT BETS
C
        IF(POL.LE.TNB4ST) THEN
          IF(NUM.EQ.NBRWIN(POL,BON+1,GIND))
     *    AMTWON=AMTWON+NBRPRZ(POL,2,GIND)*AMT
          GOTO 1100
        ENDIF
C
C CHECK BOX BETS
C
        IF(POL.GE.TNB4B24.AND.POL.LE.TNB4B4) THEN
          IF(BOXNUM.EQ.NBRWIN(POL,BON+1,GIND))
     *       AMTWON=AMTWON+NBRPRZ(POL,2,GIND)*AMT
          GOTO 1100
        ENDIF
 
C
C CHECK COMBO BETS
C
        IF(POL.GE.TNB4C24.OR.POL.LE.TNB4C4) THEN
          AMT=AMT/2
          IF(BOXNUM.EQ.NBRWIN(POL-4,BON+1,GIND))
     *       AMTWON=AMTWON+NBRPRZ(POL-4,2,GIND)*AMT
          IF(NUM.EQ.NBRWIN(TNB4ST,BON+1,GIND))
     *       AMTWON=AMTWON+NBRPRZ(TNB4ST,2,GIND)*AMT
          GOTO 1100
        ENDIF
C
C CHECK WHEEL BETS
C
        IF(POL.GE.TNB4W24.AND.POL.LE.TNB4W4) THEN
          IF(POL.EQ.TNB4W4) AMT=AMT/4
          IF(POL.EQ.TNB4W6) AMT=AMT/6
	  IF(POL.EQ.TNB4W12) AMT=AMT/12
	  IF(POL.EQ.TNB4W24) AMT=AMT/24
          IF(BOXNUM.EQ.NBRWIN(POL,BON+1,GIND))
     *       AMTWON=AMTWON+NBRPRZ(POL,2,GIND)*AMT
          GOTO 1100
        ENDIF
1100    CONTINUE
1110	CONTINUE
	IF(AMTWON.GT.REDMAX(TRABUF(TGAM))) PRVFLG=.TRUE.
        RETURN
	END
