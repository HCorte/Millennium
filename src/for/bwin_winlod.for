C     Rev 1.0   21 Jan 1993 18:06:34   DAB
C  Initial Release
C  Based on Netherlands Bible, 12/92, and Comm 1/93 update
C  DEC Baseline
C
C ** Source - winsub.for **
C
C
C SUBROUTINE TO LOAD VLF/TCF RECORDS TO DRAW FILES
C
C
C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE BWIN_WINLOD(FUN,RECORD)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
	INCLUDE 'INCLIB:DESLOG.DEF'
	INCLUDE 'INCLIB:PRMLOG.DEF'
C
        INTEGER*4 NAMVLW(5),NAMTCW(5)                              !V01
	INTEGER*4 K, TBLOCK, VBLOCK, TIND, VIND, ST, TCFCNT, VLFCNT
	INTEGER*4 VLFBUF(72,113),TCFBUF(48,170),VFDB(7),TFDB(7)
	INTEGER*4 VLFREC(8192),TCFREC(8192),RECORD(*),FUN
	EQUIVALENCE(VLFREC(1),VLFBUF(1,1))
	EQUIVALENCE(TCFREC(1),TCFBUF(1,1))
	DATA VLFREC/8192*0/,TCFREC/8192*0/
	DATA VLFCNT/0/,TCFCNT/0/
C
C
	IF(FUN.EQ.1) GOTO 100      !OPEN FILES
	IF(FUN.EQ.2) GOTO 200      !WRITE VLF RECORD
CCCCCC	IF(FUN.EQ.3) GOTO 300      !WRITE TCF RECORD
	IF(FUN.EQ.4) GOTO 400      !CLOSE FILES
C
C OPEN FILES
C
100	CONTINUE

        CALL WINGETW('BWINTSK ',NAMVLW,NAMTCW)                    !V01
	CALL OPENW(VLW,NAMVLW,4,0,0,ST)
	CALL IOINIT(VFDB,VLW,128*256)
	IF(ST.NE.0) CALL FILERR(NAMVLW,1,ST,0)
C
C
CCCCCC	CALL OPENW(TCW,NAMTCW,4,0,0,ST)
CCCCCC	CALL IOINIT(TFDB,TCW,128*256)
CCCCCC	IF(ST.NE.0) CALL FILERR(NAMTCW,1,ST,0)
C
C
	VIND=1
	TIND=1
	VBLOCK=0
	TBLOCK=0
	RETURN
C
C WRITE VLF RECORD
C
200	CONTINUE
	VLFCNT=VLFCNT+1
	CALL FASTMOV(RECORD,VLFBUF(1,VIND),72)
	VIND=VIND+1
	IF(VIND.GT.113) THEN
	  VBLOCK=VBLOCK+1
	  CALL WRITEW(VFDB,VBLOCK,VLFREC,ST)
	  IF(ST.NE.0) CALL FILERR(NAMVLW,3,ST,VBLOCK)
	  CALL FASTSET(0,VLFREC,8192)
	  VIND=1
	ENDIF
	RETURN
C
C WRITE RECORD TO CARRYOVER FILE
C
CCCCC300	CONTINUE
CCCCCC	TCFCNT=TCFCNT+1
CCCCCC	CALL FASTMOV(RECORD,TCFBUF(1,TIND),48)
CCCCCC	TIND=TIND+1
CCCCCC	IF(TIND.GT.170) THEN
CCCCCC	  TBLOCK=TBLOCK+1
CCCCCC	  CALL WRITEW(TFDB,TBLOCK,TCFREC,ST)
CCCCCC	  IF(ST.NE.0) CALL FILERR(NAMTCW,3,ST,TBLOCK)
CCCCCC	  CALL FASTSET(0,TCFREC,8192)
CCCCCC	  TIND=1
CCCCCC	ENDIF
CCCCCC	RETURN
C
C CLOSE FILES
C
400	CONTINUE
	VBLOCK=VBLOCK+1
	CALL WRITEW(VFDB,VBLOCK,VLFREC,ST)
	IF(ST.NE.0) CALL FILERR(NAMVLW,3,ST,VBLOCK)
	CALL CLOSEFIL(VFDB)
C
C
CCCCCC	TBLOCK=TBLOCK+1
CCCCCC	CALL WRITEW(TFDB,TBLOCK,TCFREC,ST)
CCCCCC	IF(ST.NE.0) CALL FILERR(NAMTCW,3,ST,TBLOCK)
CCCCCC	CALL CLOSEFIL(TFDB)
C
C
	WRITE(5,900) IAM(),VLFCNT,(NAMVLW(K),K=1,5)
CCCCCC	WRITE(5,900) IAM(),TCFCNT,(NAMTCW(K),K=1,5)
	RETURN
C
C
900	FORMAT(1X,A,1X,I8,' records loaded to ',5A4)
	END
