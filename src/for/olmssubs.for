CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C       SUBROUTINE TO GET ATTACH DATE AND TIME IN OLMCOM MEMORY
C
C       INPUT : NONE
C       OUTPUT: NONE
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C=======OPTIONS /CHECK=NOOVERFLOW
        SUBROUTINE GET_OLMS_ATTACHDATTIM()
        IMPLICIT NONE
C
        INCLUDE 'INCLIB:SYSPARAM.DEF'
        INCLUDE 'INCLIB:SYSEXTRN.DEF'
C
        INCLUDE 'INCLIB:GLOBAL.DEF'
        INCLUDE 'INCLIB:OLMCOM.DEF'
C
        CALL ICLOCK(1,OLMS_ATTACHTIM)
        CALL GDATE(OLMS_ATTACHDAT(2),
     *             OLMS_ATTACHDAT(3),
     *             OLMS_ATTACHDAT(1))
        IF(OLMS_ATTACHDAT(1).LT.77) THEN
            OLMS_ATTACHDAT(1) = OLMS_ATTACHDAT(1) + 2000
        ELSE
            OLMS_ATTACHDAT(1) = OLMS_ATTACHDAT(1) + 1900
        ENDIF
C
        END

CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C       SUBROUTINE TO GET DETATTACH DATE AND TIME IN OLMCOM MEMORY
C
C       INPUT : NONE
C       OUTPUT: NONE
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C=======OPTIONS /CHECK=NOOVERFLOW
            SUBROUTINE GET_OLMS_DETACHDATTIM()
C
            IMPLICIT NONE
C
            INCLUDE 'INCLIB:SYSPARAM.DEF'
            INCLUDE 'INCLIB:SYSEXTRN.DEF'
C
            INCLUDE 'INCLIB:GLOBAL.DEF'
            INCLUDE 'INCLIB:OLMCOM.DEF'
C
            CALL ICLOCK(1,OLMS_DETACHTIM)
            CALL GDATE(OLMS_DETACHDAT(2),
     *             OLMS_DETACHDAT(3),
     *             OLMS_DETACHDAT(1))
            IF(OLMS_DETACHDAT(1).LT.77) THEN
              OLMS_DETACHDAT(1) = OLMS_DETACHDAT(1) + 2000
            ELSE
              OLMS_DETACHDAT(1) = OLMS_DETACHDAT(1) + 1900
            ENDIF
C
            END        
C 
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C       SUBROUTINE TO RESET ATTACH DATE AND TIME IN OLMCOM MEMORY
C
C       INPUT : NONE
C       OUTPUT: NONE
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C=======OPTIONS /CHECK=NOOVERFLOW
            SUBROUTINE CLR_OLMS_ATTACHDATTIM()
C
            IMPLICIT NONE
C
            INCLUDE 'INCLIB:SYSPARAM.DEF'
            INCLUDE 'INCLIB:SYSEXTRN.DEF'
C
            INCLUDE 'INCLIB:GLOBAL.DEF'
            INCLUDE 'INCLIB:OLMCOM.DEF'
C
            CALL FASTSET(0,OLMS_ATTACHDAT,8)
            CALL FASTSET(0,OLMS_ATTACHTIM,2)
C
            END
C
