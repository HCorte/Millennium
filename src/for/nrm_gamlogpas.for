C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE GAMLOGPAS(GDRW,GIND,DSKREC,GAMREC)
	IMPLICIT NONE

	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'

	INCLUDE 'INCLIB:LPAREC.DEF'
	INCLUDE 'INCLIB:DPAREC.DEF'
C
C PARAMETERS
C
	INTEGER*4 DSKREC(*),GAMREC(*), GDRW, GIND
C
C LOCAL VARIABLES
C
	INTEGER*4 INDAUX, I4_IND
C
C CONVERT PASSIVE LOTTERY RECORD
C       
	IF(GIND.LT.1 .OR. GIND.GT.NUMPAS) RETURN
	IF(GDRW.LT.1 .OR. GDRW.GT.PAGEMI) RETURN

	CALL FASTMOV(GAMREC,LPAREC,LPARECLEN)

	DPASTS    = LPASTS(GDRW,GIND) 
	DPAEMIS   = LPAEMIS(GDRW,GIND)  
	DPANUMTCK = LPANUMTCK(GDRW,GIND)
	DPANUMSER = LPANUMSER(GDRW,GIND)
	DPAPRC    = LPAPRC(GDRW,GIND)
	DPAESD    = LPAESD(GDRW,GIND)    
	DPATIM    = LPATIM(GDRW,GIND)    
	DPACTM    = LPACTM(GDRW,GIND)    
	DPAPLAN   = LPAPLAN(GDRW,GIND)   
	DPAPRGCDC = LPAPRGCDC(GDRW,GIND)
	DPAREDAMT = LPAREDAMT(GDRW,GIND)
	DPADIV    = LPADIV(GDRW,GIND)    
	DPAEMT    = LPAEMT(GDRW,GIND)
	DPAWSER   = LPAWSER(GDRW,GIND)

	DO  I4_IND = 1, PAGDIV
	    DO	INDAUX = 1, PAGNBR
		DPAWIN(INDAUX,I4_IND) = LPAWIN(INDAUX,I4_IND,GDRW,GIND)
	    ENDDO
	ENDDO

	DO INDAUX = 1, PAGDIV
	    DPAWNUM(INDAUX)   = LPAWNUM(INDAUX,GDRW,GIND)
	    DPASHR(INDAUX)    = LPASHR(INDAUX,GDRW,GIND)
	    DPASHV(INDAUX)    = LPASHV(INDAUX,GDRW,GIND)    
	    DPAPAD(INDAUX)    = LPAPAD(INDAUX,GDRW,GIND)    
	    DPATYP(INDAUX)    = LPATYP(INDAUX,GDRW,GIND)    
	    DPADIG(INDAUX)    = LPADIG(INDAUX,GDRW,GIND)
	    DPAIDNUM(INDAUX)  = LPAIDNUM(INDAUX,GDRW,GIND)
	    DPAPRG(INDAUX)    = LPAPRG(INDAUX,GDRW,GIND)    
	ENDDO

	DO INDAUX = 1, PAGEDV
	    DPAEXSHR(INDAUX)  = LPAEXSHR(INDAUX,GDRW,GIND)
	    DPAEXSHV(INDAUX)  = LPAEXSHV(INDAUX,GDRW,GIND)
	    DPAEXPAD(INDAUX)  = LPAEXPAD(INDAUX,GDRW,GIND)
	ENDDO 

	DPACHGCDC    = LPACHGCDC(GDRW,GIND)
	DPABSD       = LPABSD(GDRW,GIND)     
	DPAPUP       = LPAPUP(GDRW,GIND)     
	DPARETCNT    = LPARETCNT(GDRW,GIND)  
	DPANOFFRA    = LPANOFFRA(GDRW,GIND)  
	DPATODPAY    = LPATODPAY(GDRW,GIND)
	DPATOPAYAMT  = LPATOPAYAMT(GDRW,GIND)
	DPARETAFTAMT = LPARETAFTAMT(GDRW,GIND)
	DPAMAXDAYPAY = LPAMAXDAYPAY(GDRW,GIND)
	DPADRAW      = LPADRAW(GDRW,GIND)

	DO  I4_IND = 1, NUMTOT
	  DO INDAUX = 1, NUMFIN
            DPAFIN(I4_IND,INDAUX) = LPAFIN(I4_IND,INDAUX,GDRW,GIND)
	  ENDDO 	
	ENDDO

        DO  I4_IND = 1, NUMTOT
	  DO INDAUX = 1, PAGDIV+PAGEDV
            DPADIVPLN(I4_IND,INDAUX) = LPADIVPLN(I4_IND,INDAUX,GDRW,GIND)
	  ENDDO 	
	ENDDO
	
	DO INDAUX = 1, 6
            DPALITDRW(INDAUX) = LPALITDRW(INDAUX,GDRW,GIND)
	ENDDO 		
	
	CALL FASTMOV(DPAREC,DSKREC,DPALEN)

	RETURN
	END
