	SUBROUTINE SETNBR(TRABUF)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
	INCLUDE 'INCLIB:NBRCOM.DEF'
	INCLUDE 'INCLIB:DESTRA.DEF'
	INTEGER*4 DIG(4), MPAIR, BOXNUM, TEMP, FLAG, SPAIR, BPAIR
	INTEGER*4 FPAIR, I, N, NTYPE, GIND, BDROFF
C
C
	GIND=TRABUF(TCMDT1)
	BDROFF=TRABUF(TCMDT2)
	NTYPE=NBRTYP(GIND)
	IF(NTYPE.EQ.NB3TYP) GOTO 1000
	IF(NTYPE.EQ.NB4TYP) GOTO 2000
	RETURN
C
C PROCESS 3-DIGIT GAME
C
1000	CONTINUE
	N=TRABUF(TCMNEW)
	DO 1010 I=3,1,-1
	DIG(I)=MOD(N,10)
	N=N/10
1010	CONTINUE
	FPAIR=DIG(1)*10+DIG(2)
	BPAIR=DIG(2)*10+DIG(3)
	SPAIR=DIG(1)*10+DIG(3)
C
1020	CONTINUE
	FLAG=0
	DO 1030 I=2,3
	IF(DIG(I).GE.DIG(I-1)) GOTO 1030
	TEMP=DIG(I)
	DIG(I)=DIG(I-1)
	DIG(I-1)=TEMP
	FLAG=1
1030	CONTINUE
	IF(FLAG.NE.0) GOTO 1020
C
C
	BOXNUM=DIG(3)+DIG(2)*10+DIG(1)*100
	NBRWIN(TNB3F2,BDROFF,GIND)=FPAIR
	NBRWIN(TNB3B2,BDROFF,GIND)=BPAIR
	NBRWIN(TNB3S2,BDROFF,GIND)=SPAIR
	NBRWIN(TNB3ST,BDROFF,GIND)=TRABUF(TCMNEW)
	NBRWIN(TNB3B6,BDROFF,GIND)=BOXNUM
	NBRWIN(TNB3B3,BDROFF,GIND)=BOXNUM
	NBRWIN(TNB3C6,BDROFF,GIND)=BOXNUM
	NBRWIN(TNB3C3,BDROFF,GIND)=BOXNUM
	NBRWIN(TNB3W6,BDROFF,GIND)=BOXNUM
	NBRWIN(TNB3W3,BDROFF,GIND)=BOXNUM
	RETURN
C
C PROCESS 4 DIGIT GAME
C
2000	CONTINUE
	N=TRABUF(TCMNEW)
	DO 2010 I=4,1,-1
	DIG(I)=MOD(N,10)
	N=N/10
2010	CONTINUE
	FPAIR=DIG(1)*10+DIG(2)
	BPAIR=DIG(3)*10+DIG(4)
	MPAIR=DIG(2)*10+DIG(3)
C
2020	CONTINUE
	FLAG=0
	DO 2030 I=2,4
	IF(DIG(I).GE.DIG(I-1)) GOTO 2030
	TEMP=DIG(I)
	DIG(I)=DIG(I-1)
	DIG(I-1)=TEMP
	FLAG=1
2030	CONTINUE
	IF(FLAG.NE.0) GOTO 2020
C
C
	BOXNUM=DIG(4)+DIG(3)*10+DIG(2)*100+DIG(1)*1000
	NBRWIN(TNB4F2,BDROFF,GIND)=FPAIR
	NBRWIN(TNB4B2,BDROFF,GIND)=BPAIR
	NBRWIN(TNB4M2,BDROFF,GIND)=MPAIR
	NBRWIN(TNB4ST,BDROFF,GIND)=TRABUF(TCMNEW)
	NBRWIN(TNB4B24,BDROFF,GIND)=BOXNUM
	NBRWIN(TNB4B12,BDROFF,GIND)=BOXNUM
	NBRWIN(TNB4B6,BDROFF,GIND)=BOXNUM
	NBRWIN(TNB4B4,BDROFF,GIND)=BOXNUM
	NBRWIN(TNB4C24,BDROFF,GIND)=BOXNUM
	NBRWIN(TNB4C12,BDROFF,GIND)=BOXNUM
	NBRWIN(TNB4C6,BDROFF,GIND)=BOXNUM
	NBRWIN(TNB4C4,BDROFF,GIND)=BOXNUM
	NBRWIN(TNB4W24,BDROFF,GIND)=BOXNUM
	NBRWIN(TNB4W12,BDROFF,GIND)=BOXNUM
	NBRWIN(TNB4W6,BDROFF,GIND)=BOXNUM
	NBRWIN(TNB4W4,BDROFF,GIND)=BOXNUM
	RETURN
	END
