C SUBROUTINE TO OPEN DRAW FILES FOR WINSEL.
C
C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE DWN_OPNDRW(FILE,UNT)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
C
	INTEGER*4 FILE(5)
C
	INTEGER*4 INPLEN
	CHARACTER*4 CXFILE
	INTEGER*4   I4FILE
	EQUIVALENCE (CXFILE,I4FILE)
C
	INTEGER*4 UNT, ST
C
10	CONTINUE
	CALL USRCLOS1(     UNT)
	CALL OPENW(UNT,FILE,5,0,0,ST)
	IF(ST.EQ.0) THEN
	  WRITE(5,930) IAM(),FILE
	  RETURN
	ENDIF
C
C
	WRITE(5,900) IAM(),FILE,ST
	CALL PRMTEXT('Enter new draw pack volume name: ',CXFILE,INPLEN)
	FILE(1) = I4FILE
	WRITE(5,920) IAM(),FILE(1)
	CALL GPAUSE
	GOTO 10
C
C
900	FORMAT(1X,A,1X,5A4,' open error> ',I4)
920	FORMAT(1X,A,1X,'Mount ',A4,' pack and continue DWNSEL')
930	FORMAT(1X,A,1X,'Scanning file ',5A4)
	END
