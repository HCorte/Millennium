C GET DATA FROM REPQUEPAS (PASSIVE QUEUE - VALIDATIONS AND RETURNED TICKETS)
C
C=======OPTIONS    /CHECK=NOOVERFLOW
	SUBROUTINE GETPASDATA(DATA,RNUMQUE)
	IMPLICIT   NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
C
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:TASKID.DEF'
	INCLUDE 'INCLIB:PRMPRO.DEF'
	INCLUDE 'INCLIB:QUECOM.DEF'
C
	INTEGER*4 ST, DATA, RNUMQUE
C
10	CONTINUE
	CALL RTL(DATA,REPQUEPAS(1,RNUMQUE),ST)
	IF (ST.NE.2) RETURN
	CALL XWAIT(20,1,ST)
	GOTO 10
	END
