C  Based on Netherlands Bible, 12/92, and Comm 1/93 update
C  DEC Baseline
C
C ** Source - witent.for **
C
C V01 07-JAN-93  TD   ADDED INDEX TO IDENTIFY THE CALLING ROUTINE.  THIS 
C		      ALLOWED ONE ROUTINE TO BE MADE FROM THE WITENT AND
C		      WITVER VERSIONS OF CANROW
C
C
C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE CANROW(ROW,N)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:RESCOM.DEF'
	INTEGER*4 ROW, K, FLAG
	INTEGER*4 N		! N=1 IF WITENT IS CALLING ROUTINE
				! N=2 IF WITVER IS CALLING ROUTINE
			        ! NOTE THAT THIS IS ALSO USED AS AN INDEX
				! INTO THE WROWSTS ARRAY
C
C
	IF (N .EQ. 1) THEN
	  IF(WROWSTS(ROW,N).EQ.GAMCAN.OR.WROWSTS(ROW,N).EQ.GAMREF) THEN
	    WRITE(5,901) IAM(),ROW,(DWINMS(K,ROW),K=1,3)
	    RETURN
	  ENDIF
	ELSE IF (N .EQ. 2) THEN
	  IF(WROWSTS(ROW,N).EQ.GAMCAN) THEN
	    WRITE(5,901) IAM(),ROW,(DWINMS(K,ROW),K=1,3)
	    RETURN
	  ENDIF
	ENDIF
C
	IF(WROWSTS(ROW,N).EQ.GAMNUL) THEN
	  TYPE*,IAM(),' Row ',ROW,' not active for this event'
	  RETURN
	ENDIF
C
	WRITE(5,902) IAM(),ROW,(DWINMS(K,ROW),K=1,3)
	CALL WIMG(5,'Is this correct (Y/N) ')
	CALL YESNO(FLAG)
	IF(FLAG.NE.1) RETURN
	WROWSTS(ROW,N)=GAMCAN
	RETURN
C
901	FORMAT(1X,A,' Row ',I2,1X,3A4,' Already cancelled')
902	FORMAT(1X,A,' Cancellation of row ',I4,1X,3A4)
	END
