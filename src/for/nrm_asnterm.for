C
C ASSIGN LOGICAL NAME
C*****************************************************************
C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE ASNTERM (LOGICAL_NAME, STATUS)
	IMPLICIT  NONE
C
	CHARACTER*(*) LOGICAL_NAME	! INPUT
	INTEGER*4 STATUS		! OUTPUT
C
	INCLUDE	    'INCLIB:SYSPARAM.DEF'
	INCLUDE	    '($SYSSRVNAM)'
	INCLUDE	    '($LNMDEF)'
C
	CHARACTER*8 TN		! LOCAL SPACE FOR TERMINAL NAME
	BYTE	    CTN(8)
	EQUIVALENCE (CTN(1), TN)
C
	INTEGER*4   TNL		! LENGTH OF THE 'TN'
C
	INTEGER*4	I4ITEM(7)
	INTEGER*2	I2ITEM(14)
	EQUIVALENCE	(I4ITEM,I2ITEM)
	INTEGER*4	ATTRIBUTES, DUMMY, ST
C
C GET TERMINAL NAME
C
	CALL GETTERM(TN, ST)
	IF (ST .NE. 0) THEN
	    TYPE *, 'VAX_GETTERM:ASNTERM: FAIILED TO GET TERM NAME '
	    STATUS = 1
	    RETURN
	ENDIF
C
C	SET PARAMETERS FOR 'CRELNM' CALL
C
	ATTRIBUTES = IOR(LNM$M_CONCEALED, LNM$M_TERMINAL)
C
	I2ITEM(1) = 4
	I2ITEM(2) = LNM$_ATTRIBUTES
	I4ITEM(2) = %LOC(ATTRIBUTES)
	I4ITEM(3) = %LOC(DUMMY)
C
	I2ITEM(7) = 7
	I2ITEM(8) = LNM$_STRING
	I4ITEM(5) = %LOC(TN)
	I4ITEM(6) = %LOC(TNL)
	I4ITEM(7) = 0		    !TO TERMINATE LIST
 
	ST = SYS$CRELNM (0,'LNM$JOB',
     *			  LOGICAL_NAME,, I4ITEM)
	IF (.NOT. ST) THEN
	    TYPE *, 'VAX_GETTERM:ASNTERM: FAILED TO ''CRELNM'' '
	    CALL LIB$SIGNAL(%VAL(ST))
	    STATUS = 1
	    RETURN
	ENDIF
C
	STATUS = 0
C
	RETURN
	END
