	SUBROUTINE RESCMD(CBUF)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INTEGER*4 CBUF(CDLEN), ST
C
C
10	CONTINUE
	CALL QUECMD(CBUF,ST)
	IF(ST.NE.0) THEN
	   TYPE*,IAM(),' Queue command error > ',ST,' continue to retry'
	   CALL GPAUSE
	   GOTO 10
	ENDIF
	RETURN
	END
