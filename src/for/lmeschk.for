C SUBROUTINE TO CHECK IF LIABILITY MESSAGE SHOULD BE PRINTED
C
C
C=======OPTIONS /CHECK=NOOVERFLOW
        SUBROUTINE LMESCHK(TYP,GIND,NUM,POOL,ST)
        IMPLICIT NONE
C
        INCLUDE 'INCLIB:SYSPARAM.DEF'
        INCLUDE 'INCLIB:SYSEXTRN.DEF'
        INCLUDE 'INCLIB:GLOBAL.DEF'
        INCLUDE 'INCLIB:POLCOM.DEF'
	INTEGER*4 TYP,GIND,NUM,POOL,ST,ENTRY,I
C
C
	ST=-1
	ENTRY=NUM*10000+POOL*100+TYP*10+GIND
	DO 10 I=1,100
	IF(ENTRY.EQ.PMESS(I)) RETURN
10	CONTINUE
	ST=0
	RETURN
	END
