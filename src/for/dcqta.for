C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE DCQTA(TRABUF,OUTTAB,CRSBUF)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
	INCLUDE 'INCLIB:DESTRA.DEF'
	INCLUDE 'INCLIB:CRSBUF.DEF'
C
        INTEGER*4 IND, LENGTH, I
	BYTE      OUTTAB(*)
C
	INTEGER*4   I4TEMP
	INTEGER*2   I2TEMP(2)
	BYTE	    I1TEMP(4)
	EQUIVALENCE (I4TEMP,I2TEMP,I1TEMP)
C
C GET LENGTH
C
	IND=2
	I4TEMP=0
	I1TEMP(1)=OUTTAB(IND+0)
	I1TEMP(2)=OUTTAB(IND+1)
	LENGTH=I4TEMP
C
C GET NUMBER OF ENTRIES
C
	IND=12
	I4TEMP=0
	I1TEMP(1)=OUTTAB(IND+0)
	I1TEMP(2)=OUTTAB(IND+1)
	CRSBUF(NBRENT)=I4TEMP
	IF(CRSBUF(NBRENT).GT.MAXQTA) CRSBUF(NBRENT)=1
	IND=IND+2
C
C CHECK ERROR CODE
C
	I4TEMP=0
	I1TEMP(1)=OUTTAB(IND+0)
	I1TEMP(2)=OUTTAB(IND+1)
	TRABUF(TIERR)=I4TEMP
	IF(TRABUF(TIERR).GT.INOER) GOTO 8000
	IF(TRABUF(TIERR).EQ.94) THEN
	  CRSBUF(NBRENT)=0
          GOTO 8000
	ENDIF
	IF(LENGTH.NE.16+CRSBUF(NBRENT)*10) THEN
	   TRABUF(TIERR)=INLTH
	   GOTO 8000
	ENDIF
	IND=IND+2
C
C GET NEXT GAME NUMBER
C
	I4TEMP=0
	I1TEMP(1)=OUTTAB(IND+0)
	I1TEMP(2)=OUTTAB(IND+1)
	CRSBUF(NXTGAM)=I4TEMP
	IND=IND+2
C
C GET GAME ID
C
	DO 100 I=0,CRSBUF(NBRENT)-1
	   I4TEMP=0
	   I1TEMP(1)=OUTTAB(IND+0)
	   I1TEMP(2)=OUTTAB(IND+1)
	   CRSBUF(QTGAME+I)=I4TEMP
	   IND=IND+2
C
C GET ON ORDER
C
	   I4TEMP=0
	   I1TEMP(1)=OUTTAB(IND+0)
	   I1TEMP(2)=OUTTAB(IND+1)
	   CRSBUF(OORDER+I)=I4TEMP
	   IND=IND+2
C
C GET QUOTA MAX
C
	   I4TEMP=0
	   I1TEMP(1)=OUTTAB(IND+0)
	   I1TEMP(2)=OUTTAB(IND+1)
	   CRSBUF(QUOTAM+I)=I4TEMP
	   IND=IND+2
C
C GET UNSOLD (TICKETS ON HAND)
C
	   I4TEMP=0
	   I1TEMP(1)=OUTTAB(IND+0)
	   I1TEMP(2)=OUTTAB(IND+1)
	   CRSBUF(UNSOLD+I)=I4TEMP
	   IND=IND+2
C
C GET REORDER MINIMUM
C
	   I4TEMP=0
	   I1TEMP(1)=OUTTAB(IND+0)
	   I1TEMP(2)=OUTTAB(IND+1)
	   CRSBUF(REORDE+I)=I4TEMP
	   IND=IND+2
100	CONTINUE
C
C
C
8000	CONTINUE
	RETURN
	END
