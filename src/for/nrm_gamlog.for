        DLTPRN=LLTPRN(GIND)
        DLTTAX=LLTTAX(GIND)
        CALL FASTMOV(LLTRES(1,GIND),DLTRES,2)
        DLTAPL=LLTAPL(GIND)
        DLTMIN=LLTMIN(GIND)
        DLTSER=LLTSER(GIND)
        DLTOPA=LLTOPA(GIND)
        DLTPRC=LLTPRC(GIND)
        DLTMAX=LLTMAX(GIND)
        DLTNUM=LLTNUM(GIND)
        DLTBFL=LLTBFL(GIND)
        DLTMLT=LLTMLT(GIND)
        DLTDIV=LLTDIV(GIND)
        CALL FASTMOV(LLTWTB(1,1,1,GIND),DLTWTB,LTGNBR*LTGBET*(LTGBON+1))
        CALL FASTMOV(LLTPER(1,GIND),DLTPER,LTGDIV)
        DLTSPR=LLTSPR(GIND)
        CALL FASTMOV(LLTTSR(1,1,GIND),DLTTSR,LTGDIV*2)
        DLTREV=LLTREV(GIND)
        DLTBDR=LLTBDR(GIND)
        DLTLST=LLTLST(GIND)
        CALL FASTMOV(LLTLSV(1,1,GIND),DLTLSV,LTGDIV*2)
        DLTADW=LLTADW(GIND)
        CALL FASTMOV(LLTLAN(1,1,GIND),DLTLAN,LTGDIV*2)
        CALL FASTMOV(LLTBET(1,GIND),DLTBET,LTGBET)
        CALL FASTMOV(LLTWED(1,GIND),DLTWED,2)
        DLTPAW=LLTPAW(GIND)
        CALL FASTMOV(LLTSTX(1,1,GIND),DLTSTX,LTGDIV*2)
        CALL FASTMOV(LLTASH(1,GIND),DLTASH,LTGDIV)
        CALL FASTMOV(LLTWRF(1,GIND),DLTWRF,10)
        DLTEST=LLTEST(GIND)     
        CALL MOVBYT(LLTMDS(1,GIND),1,DLTMDS,1,MAXMLTD_AVL)
        CALL FASTMOV(LLTBAL(1,GIND),DLTBAL,MAXDRW)
	DLTSPL=LLTSPL(GIND)
        CALL FASTMOV(LLTLNM(1,GIND),DLTLNM,MAXBDR)   !V45...
        CALL FASTMOV(LLTLHL(1,GIND),DLTLHL,MAXBDR)
        CALL FASTMOV(LLTLNC(1,1,GIND),DLTLNC,LTGDIV*2)
	DLTLFL=LLTLFL(GIND)
	DLTLDV=LLTLDV(GIND)                          !...V45
	DLTJPG=LLTJPG(GIND)                          !V45 FJG
	DLTCCC=LLTCCC(GIND)                          !V46 HXK
        CALL FASTMOV(DLTREC,DSKREC,DLTLEN)

        RETURN
C
C CONVERT SPORTS GAME RECORD
C
2000    CONTINUE
        IF(GIND.GT.NUMSPT) RETURN
        CALL FASTMOV(GAMREC,LSPREC,LSPRECLEN)
        DSPSTS=LSPSTS(GIND)
        DSPCTM=LSPCTM(GIND)
        DSPTIM=LSPTIM(GIND)
        DSPDRW=LSPDRW(GIND)
        DSPBSD=LSPBSD(GIND)
        DSPESD=LSPESD(GIND)
        DSPPUP=LSPPUP(GIND)
        DSPUPD=LSPUPD(GIND)
        CALL FASTMOV(LSPDAT(1,GIND),DSPDAT,DATLEN)
        CALL FASTMOV(LSPADV(1,GIND),DSPADV,NUMADV)
        CALL FASTMOV(LSPSAL(1,GIND),DSPSAL,SPGENT)
        CALL FASTMOV(LSPSHV(1,GIND),DSPSHV,SPGDIV)
        CALL FASTMOV(LSPSHR(1,GIND),DSPSHR,SPGDIV)
        CALL FASTMOV(LSPPOL(1,GIND),DSPPOL,SPGDIV)
        CALL FASTMOV(LSPPAD(1,GIND),DSPPAD,SPGDIV)
        CALL FASTMOV(LSPPRG(1,GIND),DSPPRG,SPGDIV)
        CALL FASTMOV(LSPAFD(1,GIND),DSPAFD,SPGDIV)
        CALL FASTMOV(LSPCSP(1,GIND),DSPCSP,SPGDIV)
        CALL FASTMOV(LSPOSV(1,GIND),DSPOSV,SPGDIV)
        CALL FASTMOV(LSPOSH(1,GIND),DSPOSH,SPGDIV)
        CALL FASTMOV(LSPWIN(1,GIND),DSPWIN,SPGNBR)
        CALL FASTMOV(LSPHLD(1,GIND),DSPHLD,SPGNBR)
        DSPPRP=LSPPRP(GIND)
        DSPPRN=LSPPRN(GIND)
        DSPTAX=LSPTAX(GIND)
        CALL FASTMOV(LSPRES(1,GIND),DSPRES,2)
        DSPAPL=LSPAPL(GIND)
        DSPMIN=LSPMIN(GIND)
        DSPSER=LSPSER(GIND)
        DSPOPA=LSPOPA(GIND)
        DSPPRC=LSPPRC(GIND)
        DSPMAX=LSPMAX(GIND)
        DSPMLT=LSPMLT(GIND)
        DSPDIV=LSPDIV(GIND)
        CALL FASTMOV(LSPMAT(1,GIND),DSPMAT,SPGDIV)
        CALL FASTMOV(LSPPER(1,GIND),DSPPER,SPGDIV)
        DSPSPR=LSPSPR(GIND)
        CALL FASTMOV(LSPTSR(1,GIND),DSPTSR,SPGDIV)
        DSPREV=LSPREV(GIND)
        CALL FASTMOV(LSPNMS(1,1,1,GIND),DSPNMS,(SPNMS_LEN/4)*2*SPGNBR)
        CALL FASTMOV(LSPCLB(1,GIND),DSPCLB,25)
        CALL FASTMOV(LSPASH(1,GIND),DSPASH,SPGDIV)
        DSPFRG=LSPFRG(GIND)
        CALL FASTMOV(LSPWRF(1,GIND),DSPWRF,POSTED)
        DSPBST=LSPBST(GIND)
        CALL MOVBYT(LSPMDS(1,GIND),1,DSPMDS,1,MAXMLTD_AVL)
        CALL FASTMOV(LSPBAL(1,GIND),DSPBAL,MAXDRW)
	CALL FASTMOV(LSPEVN(1,GIND),DSPEVN,SPEVN_LEN/4)
        DSPEXT = LSPEXT(GIND)
        CALL FASTMOV(LSPRGT(1,GIND),DSPRGT,2)
	CALL FASTMOV(LSPROD(1,GIND),DSPROD,2)
        CALL FASTMOV(LSPECD(1,GIND),DSPECD,SPGNBR)
C
        DSPMCE = LSPMCE(GIND)
        DSPDCD = LSPDCD(GIND)       
        DSPRWD = LSPRWD(GIND)       
C
        CALL FASTMOV(DSPREC,DSKREC,DSPLEN)
        RETURN
C
C CONVERT KICKER GAME RECORD
C
3000    CONTINUE
        IF(GIND.GT.NUMKIK) RETURN
        CALL FASTMOV(GAMREC,LKKREC,LKKRECLEN)
        DKKSTS=LKKSTS(GIND)
        DKKCTM=LKKCTM(GIND)
        DKKTIM=LKKTIM(GIND)
        DKKDRW=LKKDRW(GIND)
        DKKBSD=LKKBSD(GIND)
        DKKESD=LKKESD(GIND)
        DKKPUP=LKKPUP(GIND)
        DKKUPD=LKKUPD(GIND)
        CALL FASTMOV(LKKDAT(1,GIND),DKKDAT,DATLEN)
        CALL FASTMOV(LKKADV(1,GIND),DKKADV,NUMADV)
        CALL FASTMOV(LKKSAL(1,1,GIND),DKKSAL,KIGENT*MAXGAM)
        CALL FASTMOV(LKKSHR(1,GIND),DKKSHR,KIGDIV)
        CALL FASTMOV(LKKPAD(1,GIND),DKKPAD,KIGDIV)
        CALL FASTMOV(LKKPRG(1,GIND),DKKPRG,KIGDIV)
        CALL FASTMOV(LKKSHV(1,GIND),DKKSHV,KIGDIV)
        CALL FASTMOV(LKKPOL(1,1,GIND),DKKPOL,2*KIGDIV)
        CALL FASTMOV(LKKBRK(1,GIND),DKKBRK,KIGDIV)
        CALL FASTMOV(LKKFRZ(1,GIND),DKKFRZ,KIGDIV)
        DKKTAX=LKKTAX(GIND)
        CALL FASTMOV(LKKRES(1,GIND),DKKRES,2)
        DKKWIN=LKKWIN(GIND)
        DKKHLD=LKKHLD(GIND)
        DKKSER=LKKSER(GIND)
        DKKPRC=LKKPRC(GIND)
        DKKMAX=LKKMAX(GIND)
        DKKDIV=LKKDIV(GIND)
        DKKSPR=LKKSPR(GIND)
        CALL FASTMOV(LKKMAT(1,1,GIND),DKKMAT,KIGDIV*3)
        CALL FASTMOV(LKKPER(1,GIND),DKKPER,KIGDIV)
        CALL FASTMOV(LKKTSR(1,GIND),DKKTSR,KIGDIV)
        DKKREV=LKKREV(GIND)
        CALL FASTMOV(LKKSED(1,GIND),DKKSED,2)
        DKKOCT=LKKOCT(GIND)
        DKKMLT=LKKMLT(GIND)
        CALL FASTMOV(LKKWRF(1,GIND),DKKWRF,10)
        CALL FASTMOV(LKKASH(1,GIND),DKKASH,KIGDIV)
        DKKMIN=LKKMIN(GIND)
        DKKDFF=LKKDFF(GIND)
        CALL MOVBYT(LKKMDS(1,GIND),1,DKKMDS,1,MAXMLTD_AVL)
        CALL FASTMOV(LKKBAL(1,GIND),DKKBAL,MAXDRW)
	DKKSPL=LKKSPL(GIND)
        CALL FASTMOV(DKKREC,DSKREC,DKKRECLEN)
        RETURN
C
C CONVERT NUMBERS GAME RECORD
C
4000    CONTINUE
        IF(GIND.GT.NUMNBR) RETURN
        CALL FASTMOV(GAMREC,LNBREC,LNBRECLEN)
        DNBSTS=LNBSTS(GIND)
        DNBCTM=LNBCTM(GIND)
        DNBTIM=LNBTIM(GIND)
        DNBDRW=LNBDRW(GIND)
        DNBBSD=LNBBSD(GIND)
        DNBESD=LNBESD(GIND)
        DNBPUP=LNBPUP(GIND)
        DNBUPD=LNBUPD(GIND)
        CALL FASTMOV(LNBDAT(1,GIND),DNBDAT,DATLEN)
        CALL FASTMOV(LNBADV(1,GIND),DNBADV,NUMADV)
        CALL FASTMOV(LNBSAL(1,1,GIND),DNBSAL,NUMTOT*NBGPOL)
        CALL FASTMOV(LNBASL(1,1,GIND),DNBASL,NUMTOT*NBGPOL)
        CALL FASTMOV(LNBWON(1,1,1,GIND),DNBWON,NUMTOT*NBGPOL*2)
        CALL FASTMOV(LNBPAD(1,1,1,GIND),DNBPAD,NUMTOT*NBGPOL*2)
        CALL FASTMOV(LNBPRG(1,1,1,GIND),DNBPRG,NUMTOT*NBGPOL*2)
        CALL FASTMOV(LNBPRZ(1,1,GIND),DNBPRZ,NBGPOL*2)
        CALL FASTMOV(LNBWIN(1,1,GIND),DNBWIN,NBGPOL*MAXBDR)
        CALL FASTMOV(LNBHLD(1,GIND),DNBHLD,MAXBDR)
        CALL FASTMOV(LNBLIM(1,GIND),DNBLIM,3)
        DNBSER=LNBSER(GIND)
        DNBMLT=LNBMLT(GIND)
        DNBOPT=LNBOPT(GIND)
        DNBTYP=LNBTYP(GIND)
        DNBPOL=LNBPOL(GIND)
        DNBRES=LNBRES(GIND)
        DNBAPL=LNBAPL(GIND)
        DNBBRK=LNBBRK(GIND)
        DNBROL=LNBROL(GIND)
        DNBREV=LNBREV(GIND)
        DNBMAX=LNBMAX(GIND)
        DNBPRC=LNBPRC(GIND)
        DNBPER=LNBPER(GIND)
        DNBBDR=LNBBDR(GIND)
        DNBTAX=LNBTAX(GIND)
        CALL FASTMOV(DNBREC,DSKREC,DNBLEN)
        RETURN
C
C CONVERT TOTO SELECT GAME RECORD
C
5000    CONTINUE
        IF(GIND.GT.NUMTSL) RETURN
        CALL FASTMOV(GAMREC,LTSREC,LTSRECLEN)
        DTSSTS=LTSSTS(GIND)
        DTSWEK=LTSWEK(GIND)
        CALL FASTMOV(LTSDAT(1,GIND),DTSDAT,MAXSRW)
        DTSDRW=LTSDRW(GIND)
        DTSBSD=LTSBSD(GIND)
        DTSESD=LTSESD(GIND)
        DTSPUP=LTSPUP(GIND)
        DTSUPD=LTSUPD(GIND)
        CALL FASTMOV(LTSCTM(1,GIND),DTSCTM,MAXSRW)
        CALL FASTMOV(LTSTIM(1,GIND),DTSTIM,MAXSRW)
        DTSBRK=LTSBRK(GIND)
        DTSSAL=LTSSAL(GIND)
        DTSPAD=LTSPAD(GIND)
        DTSPRG=LTSPRG(GIND)
        DTSPRF=LTSPRF(GIND)
        DTSREF=LTSREF(GIND)
        DTSWON=LTSWON(GIND)
        CALL FASTMOV(LTSWBD(1,1,GIND),DTSWBD,TSLDIV*NUMTOT)
        CALL FASTMOV(LTSRBD(1,1,GIND),DTSRBD,TSLDIV*NUMTOT)
        CALL FASTMOV(LTSPBD(1,1,GIND),DTSPBD,TSLDIV*NUMTOT)
        CALL FASTMOV(LTSTBD(1,GIND),DTSTBD,TSLDIV)
        CALL FASTMOV(LTSSBD(1,1,GIND),DTSSBD,TSLDIV*NUMTOT)
        CALL FASTMOV(LTSODS(1,1,GIND),DTSODS,MAXSRW*3)
        CALL FASTMOV(LTSWIN(1,GIND),DTSWIN,MAXSRW)
        CALL FASTMOV(LTSHLD(1,GIND),DTSHLD,MAXSRW)
        DTSTAX=LTSTAX(GIND)
        CALL FASTMOV(LTSOTX(1,GIND),DTSOTX,NUMTOT)
        CALL FASTMOV(LTSMID(1,GIND),DTSMID,NUMTOT)
        CALL FASTMOV(LTSUTX(1,GIND),DTSUTX,NUMTOT)
        DTSHST=LTSHST(GIND)
        CALL FASTMOV(LTSORM(1,GIND),DTSORM,NUMTOT)
        CALL FASTMOV(LTSNMS(1,1,1,GIND),DTSNMS,(TNMS_LEN/4)*2*MAXSRW)
        CALL FASTMOV(LTSSTA(1,GIND),DTSSTA,MAXSRW)
        DTSREV=LTSREV(GIND)
        CALL FASTMOV(LTSPFN(1,GIND),DTSPFN,5)
        CALL FASTMOV(LTSTOP(1,1,GIND),DTSTOP,60)
        DTSRWS=LTSRWS(GIND)
        DTSCMB=LTSCMB(GIND)
        DTSPRC=LTSPRC(GIND)
        DTSDTE=LTSDTE(GIND)
        CALL FASTMOV(LTSTVC(1,1,GIND),DTSTVC(1,1),(TTVC_LEN/4)*MAXSRW)
        CALL MOVBYT(LTSMDS(1,GIND),1,DTSMDS,1,MAXMLTD_AVL)
        CALL FASTMOV(LTSWTOP(1,1,GIND),DTSWTOP(1,1),2*100)
        CALL MOVBYT(LTSROWTYP(1,GIND),1,DTSROWTYP,1,MAXSRW)
        CALL FASTMOV(DTSREC,DSKREC,DTSLEN)
        RETURN
C
C CONVERT WINNERS TIP GAME RECORD
C
6000    CONTINUE
        IF(GIND.GT.NUMWIT) RETURN
        CALL FASTMOV(GAMREC,LWIREC,LWIRECLEN)
        DWISTS=LWISTS(GIND)
        DWIWEK=LWIWEK(GIND)
        DWIDAT=LWIDAT(GIND)
        DWIDRW=LWIDRW(GIND)
        DWIBSD=LWIBSD(GIND)
        DWIESD=LWIESD(GIND)
        DWIPUP=LWIPUP(GIND)
        DWIUPD=LWIUPD(GIND)
        DWICTM=LWICTM(GIND)
        DWITIM=LWITIM(GIND)
        DWISER=LWISER(GIND)
