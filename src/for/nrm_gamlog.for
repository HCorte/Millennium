     *               LBNREC,LDBREC,LCPREC,LTRREC,LSSREC,
     *               LSTREC,LTGREC)
        EQUIVALENCE (DLTREC,DSPREC,DKKREC,DNBREC,DTSREC,DWIREC,DSCREC,
     *               DBNREC,DDBREC,DCPREC,DTRREC,DSSREC,
     *               DSTREC,DTGREC)

        IF(GIND.LT.1) RETURN
        IF(GTYP.EQ.TLTO) GOTO 1000
        IF(GTYP.EQ.TSPT) GOTO 2000
        IF(GTYP.EQ.TKIK) GOTO 3000
        IF(GTYP.EQ.TNBR) GOTO 4000
        IF(GTYP.EQ.TTSL) GOTO 5000
        IF(GTYP.EQ.TWIT) GOTO 6000
        IF(GTYP.EQ.TSCR) GOTO 7000
        IF(GTYP.EQ.TBNG) GOTO 10000
        IF(GTYP.EQ.TDBL) GOTO 11000
        IF(GTYP.EQ.TCPL) GOTO 12000
        IF(GTYP.EQ.TTRP) GOTO 13000
        IF(GTYP.EQ.TSSC) GOTO 14000
        IF(GTYP.EQ.TSTR) GOTO 15000
        IF(GTYP.EQ.TTGL) GOTO 16000        
        RETURN
C
C CONVERT LOTTO GAME RECORD
C
1000    CONTINUE
        IF(GIND.GT.NUMLTO) RETURN
        CALL FASTMOV(GAMREC,LLTREC,LLTRECLEN)
        DLTSTS=LLTSTS(GIND)
        DLTCTM=LLTCTM(GIND)
        DLTTIM=LLTTIM(GIND)
        DLTDRW=LLTDRW(GIND)
        DLTBSD=LLTBSD(GIND)
        DLTESD=LLTESD(GIND)
        DLTPUP=LLTPUP(GIND)
        DLTUPD=LLTUPD(GIND)
        CALL FASTMOV(LLTDAT(1,GIND),DLTDAT,DATLEN)
        CALL FASTMOV(LLTADV(1,GIND),DLTADV,NUMADV)
        CALL FASTMOV(LLTSAL(1,GIND),DLTSAL,LTGENT)
        CALL FASTMOV(LLTSHV(1,1,GIND),DLTSHV,LTGDIV*2)
        CALL FASTMOV(LLTSHR(1,1,GIND),DLTSHR,LTGDIV*2)
        CALL FASTMOV(LLTPAD(1,1,GIND),DLTPAD,LTGDIV*2)
        CALL FASTMOV(LLTPRG(1,1,GIND),DLTPRG,LTGDIV*2)
        CALL FASTMOV(LLTANU(1,1,GIND),DLTANU,LTGDIV*2)
        CALL FASTMOV(LLTMSR(1,1,GIND),DLTMSR,LTGDIV*2)
        CALL FASTMOV(LLTPOL(1,GIND),DLTPOL,LTGDIV)
        CALL FASTMOV(LLTBRK(1,GIND),DLTBRK,LTGDIV)
        CALL FASTMOV(LLTFRZ(1,GIND),DLTFRZ,LTGDIV)
        CALL FASTMOV(LLTWIN(1,1,GIND),DLTWIN,LTGNBR*MAXBDR)
        CALL FASTMOV(LLTHLD(1,1,GIND),DLTHLD,LTGNBR*MAXBDR)
        CALL FASTMOV(LLTBNM(1,1,GIND),DLTBNM,LTGBON*MAXBDR)
        CALL FASTMOV(LLTBHL(1,1,GIND),DLTBHL,LTGBON*MAXBDR)
