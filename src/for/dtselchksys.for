	SUBROUTINE DTSELCHKSYS(TRABUF)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:DESTRA.DEF'
	INCLUDE 'INCLIB:CHKSUMCM.DEF'
	INTEGER*4 SYSCNT(3,2)
	INTEGER*4 IND1, IND2, ROW	
	DATA SYSCNT/4,10,20,0,5,15/
C
C
	IF(TRABUF(TWTSEL1).LE.TRABUF(TWSYSN)) THEN
	  TRABUF(TERR)=SYNT
	  SYNTERRCOD=300
	  RETURN
	ENDIF
C
C
	IND1=TRABUF(TWTSEL1)-3
	IND2=TRABUF(TWSYSN)-2
	TRABUF(TWAMT)=SYSCNT(IND1,IND2)*TRABUF(TWTAMT1)
C
C
	IF(TRABUF(TWTAMT1).LT.1) THEN
	  TRABUF(TERR)=SYNT
	  SYNTERRCOD=310
	  RETURN
	ENDIF
	RETURN
	END
