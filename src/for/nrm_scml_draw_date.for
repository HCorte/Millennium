C       ******************************************************
        SUBROUTINE SCML_DRAW_DATE (SEMANA, ANO, MES, DIA, CDC)
C       ******************************************************
C
C	RETORNA O DIA (DOMINGO) E MES CORRESPONDENTE A SEMANA
C
        IMPLICIT NONE

        INCLUDE 'INCLIB:SYSPARAM.DEF'
        INCLUDE 'INCLIB:SYSEXTRN.DEF'
        INCLUDE 'INCLIB:GLOBAL.DEF' 
        INCLUDE 'INCLIB:CONCOM.DEF'
        INCLUDE 'INCLIB:DATBUF.DEF'

	INTEGER*4 SEMANA    !(INPUT) SS
	INTEGER*4 ANO	    !(INPUT) AAAA

	INTEGER*4 MES	    !(OUTPUT)
	INTEGER*4 DIA	    !(OUTPUT) 
	INTEGER*4 CDC       !(OUTPUT)

	INTEGER*2 DATE(12)
	INTEGER*4 CDC_FIRST_SUN
	INTEGER*4 OFFSET

	DATE(VDAY)  = 1
	DATE(VMON)  = 1
	DATE(VYEAR) = ANO
	CALL BDATE(DATE)
	
	OFFSET = 7-DATE(VDOW)
	CDC_FIRST_SUN = DATE(VCDC) + OFFSET

	CDC = CDC_FIRST_SUN + ((SEMANA-1)*7)

	DATE(VCDC) = CDC
	CALL LCDATE(DATE)
	DIA = DATE(VDAY)
	MES = DATE(VMON)

        RETURN
        END
