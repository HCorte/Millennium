C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE DCFIN(TRABUF,OUTTAB,CRSBUF)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
	INCLUDE 'INCLIB:DESTRA.DEF'
	INCLUDE 'INCLIB:CRSBUF.DEF'
C
        INTEGER*4 IND, LENGTH, I
	BYTE      OUTTAB(*)
C
	INTEGER*4   I4TEMP
	INTEGER*2   I2TEMP(2)
	BYTE	    I1TEMP(4)
	EQUIVALENCE (I4TEMP,I2TEMP(1),I1TEMP(1))
C
C GET LENGTH
C
	IND=2
	I4TEMP=0
	I1TEMP(1)=OUTTAB(IND+0)
	I1TEMP(2)=OUTTAB(IND+1)
	LENGTH=I4TEMP
C
C GET ERROR CODE
C
	IND=14
	I4TEMP=0
	I1TEMP(1)=OUTTAB(IND+0)
	I1TEMP(2)=OUTTAB(IND+1)
	TRABUF(TIERR)=I4TEMP
	IF(TRABUF(TIERR).GT.INOER) GOTO 8000

        IF(TRABUF(TRTYP).EQ.IPSET) THEN
C
C GET NUMBER OF ENTRIES
C
          IND=12
          I4TEMP=0
          I1TEMP(1)=OUTTAB(IND+0)
          I1TEMP(2)=OUTTAB(IND+1)
          CRSBUF(NBRENT)=I4TEMP
          IF(CRSBUF(NBRENT).GT.MAXPCK) CRSBUF(NBRENT)=1
          IF(LENGTH.NE.40+CRSBUF(NBRENT)*20) THEN
	    TRABUF(TIERR)=INLTH
	    GOTO 8000
          ENDIF
C
        ELSEIF(TRABUF(TRTYP).EQ.IADJR) THEN
C
C GET NUMBER OF ADJUSTMENTS
C
          IND=12
          I4TEMP=0
          I1TEMP(1)=OUTTAB(IND+0)
          I1TEMP(2)=OUTTAB(IND+1)
          CRSBUF(NBRENT)=I4TEMP
          IF(CRSBUF(NBRENT).GT.MAXADJ) CRSBUF(NBRENT)=1
          IF(LENGTH.NE.37+CRSBUF(NBRENT)*22) THEN
	    TRABUF(TIERR)=INLTH
	    GOTO 8000
          ENDIF
C
        ELSEIF(TRABUF(TRTYP).EQ.IADJT) THEN
C
          IF(LENGTH.NE.16+MAXADJ*12) THEN
	    TRABUF(TIERR)=INLTH
	    GOTO 8000
          ENDIF

	ELSEIF(LENGTH.NE.76) THEN 
	  TRABUF(TIERR)=INLTH
	  GOTO 8000
	ENDIF
C
C GET PACK SETTLED REPORT INFORMATION
C
        IF(TRABUF(TRTYP).EQ.IPSET) THEN
          IND=16
C
C GET CONTINUATION GAME NUMBER
C
          I4TEMP=0
          I1TEMP(1)=OUTTAB(IND+0)
          I1TEMP(2)=OUTTAB(IND+1)
          CRSBUF(NXTGAM)=I4TEMP
          IND=IND+2
C
C GET CONTINUATION PACK NUMBER
C
      	  I4TEMP=0
	  I1TEMP(1)=OUTTAB(IND+0)
	  I1TEMP(2)=OUTTAB(IND+1)
	  I1TEMP(3)=OUTTAB(IND+2)
	  I1TEMP(4)=OUTTAB(IND+3)
	  CRSBUF(NXTPCK)=I4TEMP
          IND=IND+4
C
C GET CONTINUATION RETAILER NUMBER
C
      	  I4TEMP=0
	  I1TEMP(1)=OUTTAB(IND+0)
	  I1TEMP(2)=OUTTAB(IND+1)
	  I1TEMP(3)=OUTTAB(IND+2)
	  I1TEMP(4)=OUTTAB(IND+3)
	  CRSBUF(NXTRET)=I4TEMP
          IND=IND+4
C
C GET BEGIN INVOICE DATE MONTH
C
	  I4TEMP=0
	  I1TEMP(1)=OUTTAB(IND+0)
	  I1TEMP(2)=OUTTAB(IND+1)
	  CRSBUF(BEGMON)=I4TEMP
	  IND=IND+2
C
C GET BEGIN INVOICE DATE DAY
C
	  I4TEMP=0
	  I1TEMP(1)=OUTTAB(IND+0)
	  I1TEMP(2)=OUTTAB(IND+1)
	  CRSBUF(BEGDAY)=I4TEMP
	  IND=IND+2
C
C GET BEGIN DATE YEAR
C
	  I4TEMP=0
	  I1TEMP(1)=OUTTAB(IND+0)
	  I1TEMP(2)=OUTTAB(IND+1)
	  CRSBUF(BEGYEAR)=I4TEMP
	  IND=IND+2
C
C GET END INVOICE DATE MONTH
C
	  I4TEMP=0
	  I1TEMP(1)=OUTTAB(IND+0)
	  I1TEMP(2)=OUTTAB(IND+1)
	  CRSBUF(ENDMON)=I4TEMP
	  IND=IND+2
C
C GET END INVOICE DATE DAY
C
	  I4TEMP=0
	  I1TEMP(1)=OUTTAB(IND+0)
	  I1TEMP(2)=OUTTAB(IND+1)
	  CRSBUF(ENDDAY)=I4TEMP
	  IND=IND+2
C
C GET END DATE YEAR
C
	  I4TEMP=0
	  I1TEMP(1)=OUTTAB(IND+0)
	  I1TEMP(2)=OUTTAB(IND+1)
	  CRSBUF(ENDYEAR)=I4TEMP
	  IND=IND+2
C
C GET PACK SETTLED AMOUNT
C
      	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  I1TEMP(3)=ZEXT(OUTTAB(IND+2))
	  I1TEMP(4)=ZEXT(OUTTAB(IND+3))
	  CRSBUF(PCKAMT)=I4TEMP
          IND=IND+4
C
          DO 100 I=0,CRSBUF(NBRENT)-1
C
C GET PACK NUMBER
C
             I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     I1TEMP(3)=OUTTAB(IND+2)
	     I1TEMP(4)=OUTTAB(IND+3)
             CRSBUF(PCKNUM+I)=I4TEMP
             IND=IND+4
C
C GET ORDER NUMBER (FIRST 9 DIGITS)
C
      	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     I1TEMP(3)=OUTTAB(IND+2)
	     I1TEMP(4)=OUTTAB(IND+3)
	     CRSBUF(ORDNUM1+I)=I4TEMP
             IND=IND+4
C
C GET ORDER NUMBER (LAST 4 DIGITS)
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(ORDNUM2+I)=I4TEMP
             IND=IND+2
C
C GET GAME NUMBER
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GAMNUM+I)=I4TEMP
	     IND=IND+2
C
C GET DATE MONTH
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(PCKMON+I)=I4TEMP
	     IND=IND+2
C
C GET DATE DAY
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(PCKDAY+I)=I4TEMP
	     IND=IND+2
C
C GET DATE YEAR
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(PCKYEAR+I)=I4TEMP
	     IND=IND+2
C
C GET AUTO SETTLED FLAG
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(AUTFLG+I)=I4TEMP
	     IND=IND+2
100       CONTINUE
C
C GET ADJUSTMENT REPORT INFORMATION
C
        ELSEIF(TRABUF(TRTYP).EQ.IADJR) THEN
          IND=16
C
C RETAILER NUMBER
C
          IND=IND+4
C
C MORE FLAG (0=No, 1=Yes)
C
          I4TEMP=0
          I1TEMP(1)=OUTTAB(IND+0)
          CRSBUF(MFLAG)=I4TEMP
          IND=IND+1
C
C GET CONTINUATION DATE
C
      	  I4TEMP=0
	  I1TEMP(1)=OUTTAB(IND+0)
	  I1TEMP(2)=OUTTAB(IND+1)
	  I1TEMP(3)=OUTTAB(IND+2)
	  I1TEMP(4)=OUTTAB(IND+3)
	  CRSBUF(NXTDAT)=I4TEMP
          IND=IND+4
C
C GET CONTINUATION TIME
C
      	  I4TEMP=0
	  I1TEMP(1)=OUTTAB(IND+0)
	  I1TEMP(2)=OUTTAB(IND+1)
	  I1TEMP(3)=OUTTAB(IND+2)
	  I1TEMP(4)=OUTTAB(IND+3)
	  CRSBUF(NXTTIM)=I4TEMP
          IND=IND+4
C
C GET CONTINUATION TYPE
C
          I4TEMP=0
          I1TEMP(1)=OUTTAB(IND+0)
          I1TEMP(2)=OUTTAB(IND+1)
          CRSBUF(NXTTYP)=I4TEMP
          IND=IND+2
C
C GET BEGIN INVOICE CDC
C
          I4TEMP=0
          I1TEMP(1)=OUTTAB(IND+0)
          I1TEMP(2)=OUTTAB(IND+1)
          CRSBUF(BEGCDC)=I4TEMP
          IND=IND+2
C
C GET END INVOICE CDC
C
          I4TEMP=0
          I1TEMP(1)=OUTTAB(IND+0)
          I1TEMP(2)=OUTTAB(IND+1)
          CRSBUF(ENDCDC)=I4TEMP
          IND=IND+2
C
C GET TOTAL ADJUSTMENT AMOUNT
C
          I4TEMP=0
          I1TEMP(1)=OUTTAB(IND+0)
          I1TEMP(2)=OUTTAB(IND+1)
          I1TEMP(3)=OUTTAB(IND+2)
          I1TEMP(4)=OUTTAB(IND+3)
          CRSBUF(ADJTOT)=I4TEMP
          IND=IND+4
C
          DO 200 I=0,CRSBUF(NBRENT)-1
C
C GET ADJUSTMENT DATE CDC
C
             I4TEMP=0
             I1TEMP(1)=OUTTAB(IND+0)
             I1TEMP(2)=OUTTAB(IND+1)
             CRSBUF(ADJCDC+I)=I4TEMP
             IND=IND+2
C ADJUSTMENT TEXT
C
            I4TEMP=0
            I1TEMP(1)=OUTTAB(IND+0)
            I1TEMP(2)=OUTTAB(IND+1)
            I1TEMP(3)=OUTTAB(IND+2)
            I1TEMP(4)=OUTTAB(IND+3)
            CRSBUF(ADJTXT1+I)=I4TEMP
            IND = IND + 4
C
            I4TEMP=0
            I1TEMP(1)=OUTTAB(IND+0)
            I1TEMP(2)=OUTTAB(IND+1)
            I1TEMP(3)=OUTTAB(IND+2)
            I1TEMP(4)=OUTTAB(IND+3)
            CRSBUF(ADJTXT2+I)=I4TEMP
            IND = IND + 4
C
            I4TEMP=0
            I1TEMP(1)=OUTTAB(IND+0)
            I1TEMP(2)=OUTTAB(IND+1)
            I1TEMP(3)=OUTTAB(IND+2)
            I1TEMP(4)=OUTTAB(IND+3)
            CRSBUF(ADJTXT3+I)=I4TEMP
            IND = IND + 4

            I4TEMP=0
            I1TEMP(1)=OUTTAB(IND+0)
            I1TEMP(2)=OUTTAB(IND+1)
            I1TEMP(3)=OUTTAB(IND+2)
            I1TEMP(4)=OUTTAB(IND+3)
            CRSBUF(ADJTXT4+I)=I4TEMP
            IND = IND + 4
C
C GET ADJUSTMENT AMOUNT
C
            I4TEMP=0
            I1TEMP(1)=OUTTAB(IND+0)
            I1TEMP(2)=OUTTAB(IND+1)
            I1TEMP(3)=OUTTAB(IND+2)
            I1TEMP(4)=OUTTAB(IND+3)
            CRSBUF(ADJAMT+I)=I4TEMP           !DJO - GET SIGN INFO
            IND=IND+4
200       CONTINUE
        ELSE                                        ! invoice IINVR
          IND=18
C
C GET TOTAL PACKS SOLD (COUNT)
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  I1TEMP(3)=ZEXT(OUTTAB(IND+2))
	  I1TEMP(4)=ZEXT(OUTTAB(IND+3))
	  CRSBUF(STRSAL)=I4TEMP
	  IND=IND+4
C
C GET TOTAL PACK SOLD (AMT)
C  
          I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  I1TEMP(3)=ZEXT(OUTTAB(IND+2))
	  I1TEMP(4)=ZEXT(OUTTAB(IND+3))
	  CRSBUF(STRSAL+1)=I4TEMP
	  IND=IND+4
C
C GET TOTAL CASHED (COUNT)
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  I1TEMP(3)=ZEXT(OUTTAB(IND+2))
	  I1TEMP(4)=ZEXT(OUTTAB(IND+3))
	  CRSBUF(STRSAL+2)=I4TEMP
	  IND=IND+4
C
C GET TOTAL CASHED (AMT)
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  I1TEMP(3)=ZEXT(OUTTAB(IND+2))
	  I1TEMP(4)=ZEXT(OUTTAB(IND+3))
	  CRSBUF(STRSAL+3)=I4TEMP
	  IND=IND+4
C
C GET TOTAL RETURN (AMT)
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  I1TEMP(3)=ZEXT(OUTTAB(IND+2))
	  I1TEMP(4)=ZEXT(OUTTAB(IND+3))
	  CRSBUF(STRSAL+4)=I4TEMP
	  IND=IND+4
C
C GET SALES COMMISION (AMT)
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  I1TEMP(3)=ZEXT(OUTTAB(IND+2))
	  I1TEMP(4)=ZEXT(OUTTAB(IND+3))
	  CRSBUF(STRSAL+5)=I4TEMP
	  IND=IND+4
C
C GET PRIZE COMMISION (AMT)
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  I1TEMP(3)=ZEXT(OUTTAB(IND+2))
	  I1TEMP(4)=ZEXT(OUTTAB(IND+3))
	  CRSBUF(STRSAL+6)=I4TEMP
	  IND=IND+4
C
C GET NET ADJUSTMENT (AMT)
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  I1TEMP(3)=ZEXT(OUTTAB(IND+2))
	  I1TEMP(4)=ZEXT(OUTTAB(IND+3))
	  CRSBUF(STRSAL+7)=I4TEMP
	  IND=IND+4
C
C GET TOTAL AMOUNT DUE
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  I1TEMP(3)=ZEXT(OUTTAB(IND+2))
	  I1TEMP(4)=ZEXT(OUTTAB(IND+3))
	  CRSBUF(STRSAL+8)=I4TEMP
	  IND=IND+4
C
C GET DATE MONTH
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  CRSBUF(STRSAL+9)=I4TEMP
	  IND=IND+2
C
C GET DATE DAY
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  CRSBUF(STRSAL+10)=I4TEMP
	  IND=IND+2
C
C GET DATE YEAR
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  CRSBUF(STRSAL+11)=I4TEMP
	  IND=IND+2
C
C GET BEGIN INVOICE MONTH
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  CRSBUF(STRSAL+12)=I4TEMP
	  IND=IND+2
C
C GET BEGIN INVOICE DAY
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  CRSBUF(STRSAL+13)=I4TEMP
	  IND=IND+2
C
C GET BEGIN INVOICE YEAR
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  CRSBUF(STRSAL+14)=I4TEMP
	  IND=IND+2
C
C GET END INVOICE MONTH
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  CRSBUF(STRSAL+15)=I4TEMP
	  IND=IND+2
C
C GET END INVOICE DAY
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  CRSBUF(STRSAL+16)=I4TEMP
	  IND=IND+2
C
C GET END INVOICE YEAR
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  CRSBUF(STRSAL+17)=I4TEMP
	  IND=IND+2
C
C FILLER
C
	  IND=IND+2
C
C GET DOCUMENT NUMBER
C
	  I4TEMP=0
	  I1TEMP(1)=ZEXT(OUTTAB(IND+0))
	  I1TEMP(2)=ZEXT(OUTTAB(IND+1))
	  I1TEMP(3)=ZEXT(OUTTAB(IND+2))
	  I1TEMP(4)=ZEXT(OUTTAB(IND+3))
	  CRSBUF(STRSAL+18)=I4TEMP
	  IND=IND+4
C
        ENDIF
C
C
C
8000	CONTINUE
	RETURN
	END
