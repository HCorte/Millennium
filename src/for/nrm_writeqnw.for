C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE WRITEQNW(FDB, BLOCK, BUFFER)
	IMPLICIT NONE
C
	INCLUDE	'INCLIB:SYSPARAM.DEF'
	INCLUDE	'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:DISKQIO.DEF'
C
	INTEGER*4   FDB(7)
	INTEGER*4   BLOCK
	INTEGER*4   BUFFER(*)
C
	INTEGER*4   STATUS
	INTEGER*4   FLAG
C
	CALL LIB$GET_EF(FLAG)			    !GET AN EVENT FLAG TO USE
	IF(FLAG.EQ.-1)THEN
	  FDB(FDB_EVNT) = -1
	  STATUS = -99
	  GOTO 9000
	ENDIF
	FDB(FDB_EVNT) = FLAG
C
	CALL WRITEQNWX(FDB, BLOCK, BUFFER, STATUS)
C
	IF(.NOT.STATUS)THEN
	  CALL LIB$FREE_EF(FLAG)		    !ERROR...FREE THE FLAG
	  FDB(FDB_EVNT) = -1
	  GOTO 9000
	ENDIF
C
	STATUS = 0                ! FOR COMPATIBILITY
C
C
9000	CONTINUE
	RETURN
	END
