	SUBROUTINE VALERR(STATUS,FILE,SERIAL)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
	INCLUDE 'INCLIB:TASKID.DEF'
	INTEGER*4 MESS(EDLEN), SERIAL, FILE, STATUS
C
	MESS(1)=VAL
	MESS(2)=TEGEN
	MESS(3)=16
	CALL FASTMOV(SFNAMES(1,FILE),MESS(4),5)
	MESS(9)=STATUS
	MESS(10)=SERIAL
	CALL QUEMES(MESS)
	RETURN
	END
