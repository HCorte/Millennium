C GENERATE DUMMY VLF FILE FOR REPORT TESTING
C
C
C=======OPTIONS /CHECK=NOOVERFLOW
	PROGRAM DUMVLF
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
C
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
	INCLUDE 'INCLIB:DESVAL.DEF'
	INCLUDE 'INCLIB:VALFIL.DEF'
	INCLUDE 'INCLIB:VDETAIL.DEF'
	INCLUDE 'INCLIB:PRGREC.DEF'
        INCLUDE 'INCLIB:PRMHSH.DEF'
C
        INTEGER*4 NEWBUF(I4BUCSIZ)
C
	INTEGER*4 ST, I, VBNS, MASK
C
C
	INTEGER*4 CFDB(7),UFDB(7)
	INTEGER*4 SVALREC(VALLEN),PVALREC(VALLEN)
	INTEGER*4 INDLEN(4)
        DATA INDLEN/Z00000000,Z40000000,Z80000000,ZC0000000/
        DATA MASK/ZC0000000/
C
C OPEN VLF FILE
C
C
        CALL IOPEN(SFNAMES(1,VLF),VLF,VFLEN*2,VFSCDC,VFSSER*2-1,ST)
        IF(ST.NE.0) THEN
          CALL FILERR(SFNAMES(1,VLF),1,ST,0)
          CALL GPAUSE
        ENDIF       
	VALREC(VSCDC)=1
	VALREC(VSTER)=1
	VALREC(VSTAT)=VCXL
	VALREC(VEXP)=1
	DO 1000 I=1,400
	VALREC(VGAM)=MOD(I,8)+1
	VALREC(VSSER)=I*4
	VALREC(VCSER)=I*5
	VALREC(VRAMT)=0
	VALREC(VPAMT)=0
	VALREC(VKPAMT)=0
	VALREC(VTAMT)=0
	VALREC(VCTER)=1
	VALREC(VLTER)=1
	VALREC(VGTYP)=GNTTAB(GAMTYP,VALREC(VGAM))
	VALREC(VGIND)=GNTTAB(GAMIDX,VALREC(VGAM))
	IF(MOD(I,2).EQ.0) THEN
	  VALREC(VPAMT)=10000
	  VALREC(VSSER)=I
	  VALREC(VCSER)=I*10
	  VALREC(VCCDC)=1
	ELSE
	  VALREC(VPAMT)=200000
	  VALREC(VLSER)=I*9
	  VALREC(VLCDC)=1
	ENDIF
	IF(MOD(I,4).EQ.0) VALREC(VRAMT)=10
	IF(MOD(I,8).EQ.0) VALREC(VTAMT)=4000
	VALREC(VPZOFF)=1
	VDETAIL(VDIV,1)=1
	VDETAIL(VSHR,1)=50
	VDETAIL(VBDR,1)=0
	VDETAIL(VREF,1)=0
	VDETAIL(VDRW,1)=1
	CALL DVALLOG(VALREC,VDETAIL)
C
C WRITE RECORD TO VALIDATION FILE
C
	CALL VALLOG(VALREC,V4BUF)
        CALL ISWRIT(V4BUF,VLF,NEWBUF,ST)
        IF(ST.NE.0) CALL FILERR(SFNAMES(1,VLF),3,ST,0)
1000	CONTINUE
        CALL ICLOSE(VLF,NEWBUF,ST)
        IF(ST.NE.0) CALL FILERR(SFNAMES(1,VLF),4,ST,0)
	END
