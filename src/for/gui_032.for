C
C Input parameters:
C	NONE               
C
C Output parameters:
C
C	BYTE		OUTBUF(*)    OUTPUT MESSAGE
C	INTEGER*4	MES_LEN	     MESSAGE LENGTH
C	INTEGER*4	RET_CODE:
C		0		-  no error, message accepted;
C		value >= 11	-  error number to be sent to Client.
C
C
C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE GUI_032(OUTBUF,MES_LEN,RET_CODE)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'

	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
	INCLUDE 'INCLIB:GTNAMES.DEF'
	INCLUDE 'INCLIB:GUIMPRM.DEF'
	INCLUDE 'INCLIB:GUISTR.DEF'
	INCLUDE 'INCLIB:TGLCOM.DEF'
	INCLUDE 'INCLIB:DTGREC.DEF'
	INCLUDE 'INCLIB:GUIARGS.DEF'
	INCLUDE 'INCLIB:GUIFIL.DEF'
C
C
	BYTE		OUTBUF(*)
	INTEGER*4	MES_LEN,RET_CODE
C
	INTEGER*4 NUM_COLS, NUM_ROWS
	INTEGER*4 GIND

        INTEGER*4  LPAD(TGGDIV)
        INTEGER*4  LVAL(TGGDIV)
        INTEGER*4  LWIN(TGGDIV)
 	REAL*8	   POOL
	INTEGER*4  TOTWON(2),TOTPAD(2)
        INTEGER*4  FRAMT
	EXTERNAL   FRAMT
C
        INTEGER*4  GNUM
        INTEGER*4  ST,STS
	INTEGER*4  DRAW,I,YEAR,WEK,LTOT

        CHARACTER*8 DNAME(TGGDIV)          

        CHARACTER WIN(0:3)
        DATA WIN/'0','1','2','M'/
C
C  GET PARAMETERS
C
	CALL GUI_GETPARAMS(OUTBUF, ST)
	IF(ST.NE.0) THEN
	   RET_CODE = 11
	   RETURN
	ENDIF

	GIND = GUI_ARGVAL(1)
	DRAW = GUI_ARGVAL(2)

	IF(GIND.LT.1.OR.GIND.GT.NUMTGL) THEN
	  RET_CODE = 11
	  RETURN
	ENDIF

	GNUM = GTNTAB(TTGL,GIND)
	IF(GNUM.LT.1.OR.GNUM.GT.MAXGAM) THEN
	  RET_CODE = 11
	  RETURN
	ENDIF

	IF(DRAW.LT.1) DRAW = DAYDRW(GNUM)	
	IF(DRAW.EQ.0) DRAW = DAYHDR(GNUM)
C
C GET DATA FROM COMMON OR DISK
C
5       CONTINUE
	IF(DRAW.EQ.DAYDRW(GNUM)) THEN
	    CALL GAMLOG(TTGL,GIND,DTGREC,TGLSTS)
	    GOTO 100
	ENDIF
C
C SET FOR NOT ACTIVE GAMES
C
        IF(DRAW .EQ. DAYHDR(GNUM) .AND. DAYDRW(GNUM) .LE. 0) THEN
            CALL GAMLOG(TTGL,GIND,DTGREC,TGLSTS)
            GOTO 100
        ENDIF	
C
	CALL READW(GAMFDB(1,GNUM),DRAW,DTGREC,ST)
	IF(ST.NE.0) THEN
	    CALL OPS('Failed to read '//CGFNAMES(GNUM),ST,DRAW)
	    RET_CODE = 11
	    RETURN
	ENDIF
C
100	CONTINUE
C
	LTOT = 0
	DO I = 1, TGGENT
	    LTOT = LTOT + DTGSAL(I)
        END DO
        POOL=LTOT*CALPER(DTGSPR)
        DO I=1,DTGDIV
            LVAL(I)=IDNINT(POOL*CALPER(DTGPER(I)))
            LVAL(I)=LVAL(I)+DTGPOL(I)        !ADD POOL CARRIED OVER
            LWIN(I)=0
            LPAD(I)=0
        END DO
C
        IF(DTGSTS.GE.GAMDON.OR.DTGSPR.EQ.0) THEN
            DO I = 1, DTGDIV
                LVAL(I) = DTGSHV(I)
                LWIN(I) = DTGSHR(I)*LVAL(I)
                LPAD(I) = FRAMT(MAXFRC(GNUM),DTGPAD(I),LVAL(I))
            END DO
        ENDIF
C
	CALL GUI_CHKSTS_RESCOM(GIND,TTGL,DRAW,STS)
	STS = MAX(STS,DTGSTS)
C
C BUILD GUI MESSAGE
C
	CALL GUIARG_INIT()

	NUM_COLS = 8  
	NUM_ROWS = 1
	CALL GUIARG_NEXT_SET(OUTBUF,NUM_COLS)

        CALL FIGWEK(DTGESD,WEK,YEAR)

	CALL GUIARG_CHAR(OUTBUF,GLNAMES(1,GNUM),16)
	CALL GUIARG_INT2(OUTBUF,DTGDRW)
	CALL GUIARG_INT2(OUTBUF,WEK)
	CALL GUIARG_INT2(OUTBUF,YEAR)

	CALL GUIARG_BYTE(OUTBUF,STS)
	CALL GUIARG_MONY(OUTBUF,DTGSAL(1))
	CALL GUIARG_MONY(OUTBUF,DTGPOL(1))	
	CALL GUIARG_MONY(OUTBUF,LTOT)	
C
C RESULT SET 2
C
	NUM_COLS = 2
	NUM_ROWS = DTGDIV
	CALL GUIARG_NEXT_SET(OUTBUF,NUM_COLS)

        DO I=1,TGGDIV
           IF(DTGMAT(I).NE.0) THEN
	      WRITE (DNAME(I),800) I,DTGMAT(I)
	   ELSE
	      WRITE(DNAME(I),801) I
	   ENDIF
         END DO
	
	DO I=1,DTGDIV
 	   CALL GUIARG_CHAR(OUTBUF,%REF(DNAME(I)),8)
       	   CALL GUIARG_MONY(OUTBUF,LVAL(I))
	ENDDO	
C
C RESULT SET 3
C
	NUM_COLS = 2
	NUM_ROWS = (DTGDAT(CURDRW) - DTGBSD) + 1    
	CALL GUIARG_NEXT_SET(OUTBUF,NUM_COLS)

	DO I=1, NUM_ROWS
          CALL GUIARG_DATE(OUTBUF,DTGDAT(CURDRW)-I+1)
          CALL GUIARG_MONY(OUTBUF,DTGSAL(2+I))
	ENDDO
C
	IF(DTGSTS.LT.GAMENV) GOTO 9000
C
C RESULT SET 4
C
	NUM_COLS = 2
	NUM_ROWS = DTGMAX
	CALL GUIARG_NEXT_SET(OUTBUF,NUM_COLS)

	DO I=1,NUM_ROWS
	   CALL GUIARG_CHAR(OUTBUF,%REF(WIN(DTGWIN(1,I))),1)
	   CALL GUIARG_CHAR(OUTBUF,%REF(WIN(DTGWIN(2,I))),1)
	ENDDO
C
	IF(DTGSTS.LT.GAMDON) GOTO 9000
C
	TOTWON(1) = 0
	TOTWON(2) = 0
	TOTPAD(1) = 0
	TOTPAD(2) = 0
        DO I=1,DTGDIV
            TOTWON(1) = TOTWON(1) + DTGSHR(I)
            TOTWON(2) = TOTWON(2) + LWIN(I)
            TOTPAD(1) = TOTPAD(1) + DTGPAD(I)
            TOTPAD(2) = TOTPAD(2) + LPAD(I)
        END DO
C
C RESULT SET 5
C
	NUM_COLS = 4
	NUM_ROWS = DTGDIV
        CALL GUIARG_NEXT_SET(OUTBUF,NUM_COLS)

	DO I=1,DTGDIV
 	   CALL GUIARG_INT4(OUTBUF,DTGSHR(I))
	   CALL GUIARG_MONY(OUTBUF,LWIN(I))
	   CALL GUIARG_INT4(OUTBUF,DTGPAD(I))
	   CALL GUIARG_MONY(OUTBUF,LPAD(I))
	ENDDO
C	
C RESULT SET 6
C
	NUM_COLS = 4
	NUM_ROWS = 1
        CALL GUIARG_NEXT_SET(OUTBUF,NUM_COLS)
C
	CALL GUIARG_INT4(OUTBUF,TOTWON(1))
	CALL GUIARG_MONY(OUTBUF,TOTWON(2))
	CALL GUIARG_INT4(OUTBUF,TOTPAD(1))
	CALL GUIARG_MONY(OUTBUF,TOTPAD(2))
C
9000	CONTINUE
	CALL GUIARG_SET_MESLEN(MES_LEN)
	RETURN
C
800     FORMAT(I1,1X,'(',I3,')')
801     FORMAT(I1,1X,'( - )')
	END
