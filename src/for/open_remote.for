	SUBROUTINE OPEN_REMOTE(LUN, FILENAME, SHRIND, DUM1, DUM2, ST)
	IMPLICIT NONE
C
	INCLUDE	    'INCLIB:SYSPARAM.DEF'
	INCLUDE	    'INCLIB:SYSEXTRN.DEF'
	INCLUDE	    'INCLIB:DISKIO.DEF'
	INCLUDE	'($SYSSRVNAM)'
	INCLUDE '($FABDEF)'
	INCLUDE '($STSDEF)'
	INCLUDE '($RMSDEF)'
C
	INTEGER*4   LUN
	CHARACTER   FILENAME*(*)
	INTEGER*4   SHRIND
	INTEGER*4   DUM1
	INTEGER*4   DUM2
	INTEGER*4   ST
	
	LOGICAL*1    ISTHERE
	CHARACTER*20 ISDIRECT
	CHARACTER*20 ORGTYPE
	CHARACTER*20 RECTYPE
C
	INTEGER*4   DISKOPEN
	EXTERNAL    DISKOPEN
C
C
C
C
C
C If the file doesn't exist, just return with status = -1
C
	INQUIRE(FILE=FILENAME, EXIST=ISTHERE,
     *	            IOSTAT=ST,
     *	          DIRECT=ISDIRECT,
     *	          ORGANIZATION=ORGTYPE,
     *	          RECORDTYPE=RECTYPE)
	IF(.NOT.ISTHERE)THEN
	  ST = -1
C***	  TYPE *,IAM(),'FILE NOT FOUND: ', FILENAME
C***	  CALL LIB$SIGNAL(%VAL(RMS$_FNF))
	  GOTO 9000
	ENDIF
C
C Save the file name for RENAME(LUN, NAME, ...) option
C
	FNAMES(LUN) = FILENAME
C
C Now open the file
C
	IF(ISDIRECT.EQ.'YES' .AND.
     *	     ORGTYPE.EQ.'RELATIVE' .AND.
     *	     RECTYPE.EQ.'FIXED')THEN
C
	  IF(SHRIND.EQ.0)THEN
   	    OPEN(UNIT=LUN, FILE=FILENAME, ORGANIZATION='RELATIVE',
     1	           ACCESS='DIRECT', STATUS='OLD', IOSTAT=ST,
     2	           USEROPEN=DISKOPEN, SHARED)
	  ELSE
   	    OPEN(UNIT=LUN, FILE=FILENAME, ORGANIZATION='RELATIVE',
     1	           ACCESS='DIRECT', STATUS='OLD', IOSTAT=ST,
     2	           USEROPEN=DISKOPEN, SHARED)
C
	  ENDIF
	ELSE
	  IF(SHRIND.EQ.0)THEN
	    OPEN(UNIT=LUN, FILE=FILENAME, STATUS='OLD',
     *	           IOSTAT=ST, SHARED, READONLY)
	  ELSE
	    OPEN(UNIT=LUN, FILE=FILENAME, STATUS='OLD',
     *	           IOSTAT=ST, SHARED)
	  ENDIF
	ENDIF
C
	IF(ST)THEN
	  ST = 0
	ENDIF
C
C
9000	CONTINUE
	RETURN
	END
