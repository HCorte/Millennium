C IS_BLANK.FOR
C
C LOGICAL FUNCTION IS_BLANK(STRING,LENGTH)
C
C V01 08-DEC-2000 ANG INITIAL RELEASE FOR PORTUGAL
C
C DETERMINES IF A STRING IS BLANK OR NULL(EMPTY) OR FULL OF 
C WHITESPACE ( <= ASCII CODE 32)
C
C INPUTS:
C  STRING   CHARACTER ARRAY                                 CHAR (LENGTH) 
C  LENGTH   LENGTH OF STRING                                INT*4
C
C OUTPUTS:
C  IS_BLANK  TRUE IF BLANK OR WHITESPACE, FALSE OTHERWISE   LOGICAL
C
C
	LOGICAL FUNCTION IS_BLANK(STRING,LENGTH)

        IMPLICIT NONE

        LOGICAL EMPTY
        INTEGER*4 LENGTH
        INTEGER*4 STR_NDX  !INDEX ON STRING
        CHARACTER STRING(LENGTH)

        EMPTY = .TRUE. !START ASSUMING STRING EMPTY
        STR_NDX = 1

        DO WHILE(EMPTY.AND.STR_NDX.LE.LENGTH)
          IF(ICHAR(STRING(STR_NDX)).GT.32) THEN
            EMPTY = .FALSE.
          ELSE  !WHITESPACE CHARACTER
            STR_NDX = STR_NDX +1  !LOOK AT NEXT CHARACTER
          ENDIF !ENDIF(ICHAR(STRING(STR_NDX)).GT.32)
        END DO !END DO WHILE(EMPTY.AND.ICHAR(STRING(STR_NDX)).GE.32...

	IS_BLANK = EMPTY

	RETURN
	END 
