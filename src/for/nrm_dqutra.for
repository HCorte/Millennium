C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE DQUTRA (TASK, BUFNUM)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
	INCLUDE 'INCLIB:PRMPRO.DEF'
	INCLUDE 'INCLIB:QUECOM.DEF'
	INCLUDE 'INCLIB:GLIST.DEF'
C
	INTEGER*4	TASK
	INTEGER*4	BUFNUM
C
	INTEGER*4	STAT, RESULT
C
C
	CALL RTL (BUFNUM, QUETAB(1, TASK), STAT)
C
	IF (STAT .EQ. GLIST_STAT_EMPTY) THEN
	  BUFNUM = 0
	ELSE
	  CALL LIB$ADAWI(-1, ACTTSK(TASK), RESULT)
	ENDIF
C
	RETURN
	END
