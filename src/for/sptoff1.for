C SPTOFF(BETS,NUMROWS,OFFSETS) ;CONVERT SPT BET TO OFFSETS
C     IN:
C     BETS    -    BET IN I*4 FORMAT
C     NUMROWS - # OF ROWS IN BET
C     OUT:
C     OFFSET  -    COMBINATIONS OFFSET
C
C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE SPTOFF1(BETS,NUMROWS,OFFSET)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
C
	INTEGER*4 BETS(*)
	INTEGER*4 OFFSET
	INTEGER*4 CNVTAB(0:4) /0,0,1,0,2/
	INTEGER*4 BYTE, NEXT_ROW, BASE, NUMROWS
C
C
	OFFSET=1
	BASE=1                     !BASE TO CALCULATE POOL
C
	DO 10, NEXT_ROW=1,NUMROWS
	  BYTE=BETS(NEXT_ROW)
	  BYTE=CNVTAB(BYTE)             !1-ST NIBLE
C
	  OFFSET=OFFSET+BASE*BYTE
C
	  BASE=BASE*3
C
10	CONTINUE
C
	RETURN
	END
