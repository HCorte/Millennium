C RUNTSK,RUNTASK -
C	ACTIVATE THE SUBPROCESS UNDER THE NAME OF THE PROGRAM
C	WITH THE PROJECT PREFIX AND WAIT FOR COMPLETEION
C--------------------------------------------------------------------
C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE RUNTSK(NAME)
	IMPLICIT NONE
C
	BYTE	NAME(8)
	LOGICAL WFLG
C
	ENTRY RUNTASK(NAME)
	WFLG=.TRUE.
C
	CALL XRUNTSK(NAME,NAME,WFLG)
	RETURN
	END
