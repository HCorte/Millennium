C SUBROUTINE TO OPEN FILES FOR VALPRO
C
C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE OPEVAL
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:PRMVLF.DEF'
	INCLUDE 'INCLIB:DESLOG.DEF'
	INCLUDE 'INCLIB:PRMLOG.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
C
	INTEGER*4 ST, NOCHECK0
C
	COMMON /NOCHECK0/ NOCHECK0
	NOCHECK0=-1
C
C
	CALL IOPEN(SFNAMES(1,VLF),VLF,VFLEN*2,VFSCDC,VFSSER*2-1,ST)
	IF(ST.NE.0) THEN
	  CALL FILERR(SFNAMES(1,VLF),1,ST,0)
	  CALL GSTOP(GEXIT_SUCCESS)
	ENDIF
C
	CALL IOPEN(SFNAMES(1,TCF),TCF,LREC*2,LCDC,LSER*2-1,ST)
	IF(ST.NE.0) THEN
	  CALL FILERR(SFNAMES(1,TCF),1,ST,0)
	  CALL GSTOP(GEXIT_SUCCESS)
	ENDIF
C
        CALL IOPEN(SFNAMES(1,DCF),DCF,LREC*2,LCDC,LSER*2-1,ST)
        IF(ST.NE.0) THEN
          CALL FILERR(SFNAMES(1,DCF),1,ST,0)
          CALL GSTOP(GEXIT_SUCCESS)
        ENDIF
	RETURN
	END
