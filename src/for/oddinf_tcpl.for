C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C
	SUBROUTINE ODDINF_TCPL(LUN,UPDATE,STATUS)
	IMPLICIT NONE
	INCLUDE 'INCLIB:SYSDEFINE.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
	INCLUDE 'INCLIB:GTNAMES.DEF'
	INCLUDE 'INCLIB:DATBUF.DEF'
	INCLUDE 'INCLIB:RECDAF.DEF'
	INCLUDE 'INCLIB:RECSCF.DEF'
	INCLUDE 'INCLIB:DCPREC.DEF'
	INCLUDE 'INCLIB:ODDINF.DEF'
C
	INTEGER*4	LUN,STATUS
	LOGICAL*4	UPDATE
C
	COMMON		SCFREC
	INTEGER*4	ST,DRAW,FDB(7),VFDB(7),DFDB(7)
	INTEGER*4	GIND,GNUM,FLAG,HRS,MINS
	INTEGER*4	REV1,REV2,REV3,REV4,PREV3
	INTEGER*2	I2DATE(LDATE_LEN),I2DATE2(LDATE_LEN),I2DATE3(LDATE_LEN)
	INTEGER*4	I,K,ROW,EVENT,EVENT_IND
	INTEGER*4	I4TV,COUNT
	CHARACTER*4	TV
	EQUIVALENCE	(TV,I4TV)
	CHARACTER*80	TNAME
	INTEGER*4	I4TNAME(20)
	EQUIVALENCE	(TNAME,I4TNAME)
	INTEGER*4	CPL_NAME_LEN
	PARAMETER	(CPL_NAME_LEN=14)
	CHARACTER*20	CDCPPFN
	EQUIVALENCE	(CDCPPFN,DCPPFN)
	LOGICAL		SKIP,FIRST
	CHARACTER*4	TV_CHANNEL(2)
	INTEGER*4	REPLUN,PAGE
	CHARACTER*40	TITL_NAME
	CHARACTER*20	FILNAM
	CHARACTER*9	HDR_DATE
	CHARACTER*7	HDR_TIME
	INTEGER*4	HDR_WEEK,HDR_YEAR,DUMMY,MIN_STAKE
C
	SKIP = .FALSE.
	FIRST=.TRUE.
C
C Read file header
C
	READ(UNIT=LUN,IOSTAT=ST,FMT='(190A)') INF.INLINE
	IF(ST.NE.0) THEN
	    WRITE(6,918) IAM(),GTNAMES(TCPL),ST    
	    GOTO 9999
	ENDIF
	IF(INF.HDR_TYPE.NE.'0') THEN
	    TYPE*,IAM(),'Invalid record type in the file header ...'   
	    GOTO 9999
	ENDIF
	IF(INF.HDR_GAME(4:4).NE.'5') THEN
	    TYPE*,IAM(),'Invalid game code in the file header ...'   
	    GOTO 9999
	ENDIF
	HDR_DATE = INF.HDR_DATE
	HDR_TIME = INF.HDR_TIME
	READ(INF.HDR_WEEK,I4FMT) HDR_WEEK
	READ(INF.HDR_YEAR(2:),I4FMT) HDR_YEAR
C
C Read header record.
C	
	READ(UNIT=LUN,IOSTAT=ST,FMT='(190A)') INF.INLINE
	IF(ST.NE.0) THEN    
	    WRITE(6,918) IAM(),GTNAMES(TCPL),ST    
	    GOTO 9999
	ENDIF
C
C Get event header record..
C
	IF(INF.EVHDR_TYPE.NE.'1') THEN
	    TYPE*,IAM(),'Invalid record type in the event header  ...'   
	    GOTO 9999
	ENDIF
C
	IF(INF.EVHDR_GAME(4:4).NE.'5') THEN
	    TYPE*,IAM(),'Invalid game code in the event header  ...'   
	    GOTO 9999
	ENDIF
C
C Read event record. 
C
40	CONTINUE
	READ(UNIT=LUN,IOSTAT=ST,FMT='(190A)') INF.INLINE
	IF(ST.NE.0) THEN    
	    WRITE(6,918) IAM(),GTNAMES(TCPL),ST    
	    GOTO 9999
	ENDIF
50	CONTINUE
C	
	IF(INF.CPL_TYPE.EQ.'9') THEN ! end record.
	    SKIP = .FALSE.
	    GOTO 1000 
	ENDIF
	IF(SKIP.AND.(INF.CPL_TYPE.EQ.'3'.OR.INF.CPL_TYPE.EQ.'4')) GOTO 40
	IF(INF.CPL_TYPE.NE.'2') THEN
	    TYPE*,IAM(),'Invalid record type ...'   
	    GOTO 9999
	ENDIF
	SKIP = .FALSE.
C
	IF(INF.CPL_GAME(4:4).NE.'5') THEN
	    TYPE*,IAM(),'Invalid game code ...'   
	    GOTO 9999
	ENDIF
C
C Get game index
C
	READ(INF.CPL_GAME_IND,I4FMT) GIND
	IF(GIND.LE.0.OR.GIND.GT.NUMCPL) THEN
	    WRITE(6,912) IAM(),GTNAMES(TCPL),GIND
	    GOTO 9999
	ENDIF	    
C
C Open game, verification and DAF file.
C
	GNUM = SCFGTN(TCPL,GIND)    
	IF(GNUM.LE.0.OR.GNUM.GT.MAXGAM) THEN
	    WRITE(6,900) IAM(),GTNAMES(TCPL),GIND
	    GOTO 9999
	ENDIF
	CALL OPENW(1,SCFSFN(1,DAF),4,0,0,ST)
	CALL IOINIT(DFDB,1,DAFSEC*256)
	IF(ST.NE.0) CALL FILERR(SCFSFN(1,DAF),1,ST,0)
	CALL OPENW(2,SCFGFN(1,GNUM),4,0,0,ST)
	CALL IOINIT(FDB,2,DCPSEC*256)
	IF(ST.NE.0) CALL FILERR(SCFGFN(1,GNUM),1,ST,0)	
	CALL OPENW(3,SCFGVN(1,GNUM),4,0,0,ST)
	CALL IOINIT(VFDB,3,DCPSEC*256)
	IF(ST.NE.0) CALL FILERR(SCFGVN(1,GNUM),1,ST,0)	
C
C Read draw number
C
	READ(INF.CPL_DRAW_NO,I4FMT) DRAW
	IF(DRAW.LE.0) THEN
	    WRITE(6,909) IAM(),GTNAMES(TCPL),GIND,DRAW
	    GOTO 9999
	ENDIF
C
C Read the file for the previous draw
C
	PREV3 = 0
	IF((DRAW.GT.1).AND.UPDATE) THEN
	    CALL READW(FDB,DRAW-1,DCPREC,ST)
	    IF(ST.NE.0) CALL FILERR(SCFGFN(1,GNUM),2,ST,DRAW-1)
	    CALL ILBYTE(PREV3,DCPREV,2)		! Get previous text checksum
	    IF(DCPSTS.EQ.0) THEN
		WRITE(6,901) IAM(),GTNAMES(TCPL),GIND,DRAW,DRAW-1
		GOTO 9999
	    ENDIF	    
	ENDIF
C
C Read file for the current draw
C
	CALL READW(FDB,DRAW,DCPREC,ST)
	IF(ST.NE.0) CALL FILERR(SCFGFN(1,GNUM),2,ST,DRAW)
C
C Checking if draw is not already set
C
        IF(DCPSTS.EQ.GAMINF.OR.DCPSTS.EQ.GAMOPN) THEN
           WRITE(6,902) IAM(),GTNAMES(TCPL),GIND,DRAW
	   IF(UPDATE) THEN
             CALL INPYESNO('Do you want to overwrite [Y/N] ?',FLAG)
             IF(FLAG.NE.1) THEN
	       SKIP = .TRUE.
	       GOTO 1000
	     ENDIF
	   ENDIF
        ENDIF
C
C Checking if draw is closed.
C
        IF(DCPSTS.GT.GAMOPN) THEN
           WRITE(6,903) IAM(),GTNAMES(TCPL),GIND,DRAW
           GOTO 9999
	ENDIF
C
	DO I=1,MAXCPLRW
	  IF(DCPSTA(I).GT.GAMOPN) THEN
             WRITE(6,915) IAM(),GTNAMES(TCPL),GIND,I
             GOTO 9999
	  ENDIF
	ENDDO
	IF(DCPEST(1).GT.GAMOPN.OR.DCPEST(2).GT.GAMOPN) THEN
	    WRITE(6,916)  IAM(),GTNAMES(TCPL),GIND
	    GOTO 9999
	ENDIF
C
C Initialize some variables...
C
	CALL LIB$MOVC5(0,0,ICHAR(' '),SIZEOF(DCPENM),DCPENM)
	CALL LIB$MOVC5(0,0,ICHAR(' '),SIZEOF(DCPDES),DCPDES)
	CALL LIB$MOVC5(0,0,ICHAR(' '),SIZEOF(DCPNMS),DCPNMS)
	DO I=1,MAXCPLRW
	    DCPSTA(I) = 0
	ENDDO
	DO I=1,2
	    DCPEST(I) = 0
	    DCPEVD(I) = 0
	    DCPEVT(I) = 0
	ENDDO
C
C Read game start date
C
	READ(INF.CPL_SDATE(4:5),I2FMT) I2DATE(VYEAR)
	READ(INF.CPL_SDATE(6:7),I2FMT) I2DATE(VMON)
	READ(INF.CPL_SDATE(8:9),I2FMT) I2DATE(VDAY)
	CALL BDATE(I2DATE)
	DCPBSD = I2DATE(VCDC)
C
C Get draw end date
C
	READ(INF.CPL_EDATE(4:5),I2FMT) I2DATE(VYEAR)
	READ(INF.CPL_EDATE(6:7),I2FMT) I2DATE(VMON)
	READ(INF.CPL_EDATE(8:9),I2FMT) I2DATE(VDAY)
	CALL BDATE(I2DATE)
	DCPESD = I2DATE(VCDC)
C
C Set week number
C
	CALL FIGWEK(DCPESD-WEEK_OFFSET,DCPWEK,DUMMY)
C
C Get draw date
C
	READ(INF.CPL_DRAW_DATE(4:5),I2FMT) I2DATE(VYEAR)
	READ(INF.CPL_DRAW_DATE(6:7),I2FMT) I2DATE(VMON)
	READ(INF.CPL_DRAW_DATE(8:9),I2FMT) I2DATE(VDAY)
	CALL BDATE(I2DATE)
	DCPDAT = I2DATE(VCDC)
C
C Draw closing time.
C
	READ(INF.CPL_CLOS_TIME(2:3),I2FMT) HRS
	READ(INF.CPL_CLOS_TIME(4:5),I2FMT) MINS
	DCPTIM = HRS*3600+MINS*60
C
C TV-channel
C
	TV = INF.CPL_EV_TV
	DCPTVC(1) = I4TV
C
C Minimum stake
C
        READ(INF.CPL_MIN_STAKE,I6FMT) MIN_STAKE
        IF(MIN_STAKE.LT.100.OR.MIN_STAKE.GT.SCFPAR(MAXSTAKE)*DYN_BETUNIT) THEN
          WRITE(6,930)  IAM(),GTNAMES(TCPL),GIND,MIN_STAKE
          GOTO 9999
        ENDIF
        DCPPRC = MIN_STAKE/DYN_BETUNIT
C
C Read competition record.
C
	DO 100 EVENT_IND=1,2
C
	READ(UNIT=LUN,IOSTAT=ST,FMT='(190A)') INF.INLINE
	IF(ST.NE.0) THEN    
	    WRITE(6,918) IAM(),GTNAMES(TCPL),ST    
	    GOTO 9999
	ENDIF
	IF(INF.CPL_EV_TYPE.NE.'3') THEN
	    TYPE*,IAM(),'Invalid record type ...'   
	    GOTO 9999
	ENDIF
C
	IF(INF.CPL_EV_GAME(4:4).NE.'5') THEN
	    TYPE*,IAM(),'Invalid game code ...'   
	    GOTO 9999
	ENDIF
C
C Number of competitors
C	
	READ(INF.CPL_EV_COUNT,I4FMT) COUNT
	IF(COUNT.LE.0.OR.COUNT.GT.MAXCPLRW/2) THEN
	    WRITE(6,913) IAM(),GTNAMES(TCPL),GIND,COUNT
	    GOTO 9999
	ENDIF
C
C Read event number
C
	READ(INF.CPL_EV_EVENT,I4FMT) EVENT
	IF(EVENT.LT.1.OR.EVENT.GT.2) THEN
	  WRITE(6,917)	IAM(),GTNAMES(TCPL),GIND,EVENT
	  GOTO 9999
	ENDIF
C
C TV channel
C
	TV_CHANNEL(EVENT) = INF.CPL_EV_TV
C
C Event end date
C
	READ(INF.CPL_EV_EDATE(4:5),I2FMT) I2DATE(VYEAR)
	READ(INF.CPL_EV_EDATE(6:7),I2FMT) I2DATE(VMON)
	READ(INF.CPL_EV_EDATE(8:9),I2FMT) I2DATE(VDAY)
	CALL BDATE(I2DATE)
	DCPEVD(EVENT) = I2DATE(VCDC)
C
C Draw closing time.
C
	READ(INF.CPL_EV_CLOS_TIME(2:3),I2FMT) HRS
	READ(INF.CPL_EV_CLOS_TIME(4:5),I2FMT) MINS
	DCPEVT(EVENT) = HRS*3600+MINS*60
C
	DCPEST(EVENT) = GAMOPN
C
C Get event name.
C
        CALL STR$UPCASE(TNAME,INF.CPL_EV_TICKTEXT)
        CALL STR$TRANSLATE(TNAME,TNAME,TRANSLATE_TABLE,MATCH_TABLE)
        CALL MOVBYT(I4TNAME,1,DCPENM(1,EVENT),1,CPLENM_LEN)
C
C Get description lines 1 - 3
C	
        CALL STR$UPCASE(TNAME,INF.CPL_EV_TEXTLN1)
        CALL STR$TRANSLATE(TNAME,TNAME,TRANSLATE_TABLE,MATCH_TABLE)
        CALL MOVBYT(I4TNAME,1,DCPDES(1,EVENT),1,30)
C
        CALL STR$UPCASE(TNAME,INF.CPL_EV_TEXTLN2)
        CALL STR$TRANSLATE(TNAME,TNAME,TRANSLATE_TABLE,MATCH_TABLE)
        CALL MOVBYT(I4TNAME,1,DCPDES(1,EVENT),31,30)
C
        CALL STR$UPCASE(TNAME,INF.CPL_EV_TEXTLN3)
        CALL STR$TRANSLATE(TNAME,TNAME,TRANSLATE_TABLE,MATCH_TABLE)
        CALL MOVBYT(I4TNAME,1,DCPDES(1,EVENT),61,30)
C
C Read all the competitors for this event.
C
	DO I=1,COUNT
	  READ(UNIT=LUN,IOSTAT=ST,FMT='(190A)') INF.INLINE
	  IF(ST.NE.0) THEN    
	    WRITE(6,918) IAM(),GTNAMES(TCPL),ST    
	    GOTO 9999
	  ENDIF
	  IF(INF.CPL_TYPE2.NE.'4') THEN
	    TYPE*,IAM(),'Invalid competitor record type ...'
	    GOTO 9999
	  ENDIF
	  IF(INF.CPL_GAME(4:4).NE.'5') THEN
            TYPE*,IAM(),'Invalid competitor game code ...'
            GOTO 9999
          ENDIF
C
C Read competiton number.
C
	  READ(INF.CPL_COMPETITION,I4FMT) EVENT
	  IF(EVENT.LT.1.OR.EVENT.GT.2) THEN
	    WRITE(6,917) IAM(),GTNAMES(TCPL),GIND,EVENT
	    GOTO 9999
	  ENDIF
C
C Read competitor number
C
	  READ(INF.CPL_ROW_NO,I4FMT) ROW
	  IF(ROW.LE.0.OR.ROW.GT.MAXCPLRW/2) THEN
	    WRITE(6,914) IAM(),GTNAMES(TCPL),GIND,ROW
	    GOTO 9999
	  ENDIF
C
	  ROW = ROW + (MAXCPLRW/2)*(EVENT-1)
	  DCPSTA(ROW) = GAMOPN
C
C Read competitor name.
C
      	  CALL STR$UPCASE(TNAME,INF.CPL_ROW_NAME)
          CALL STR$TRANSLATE(TNAME,TNAME,TRANSLATE_TABLE,MATCH_TABLE)
          CALL MOVBYT(I4TNAME,1,DCPNMS(1,ROW),1,CPLNMS_LEN)
C
	ENDDO
100	CONTINUE
C
C Read next record, to be sure that COUNT had correct value....
C
	READ(UNIT=LUN,IOSTAT=ST,FMT='(190A)') INF.INLINE
	IF(ST.NE.0) THEN    
	    WRITE(6,918) IAM(),GTNAMES(TCPL),ST    
	    GOTO 9999
	ENDIF
C
	IF(INF.CPL_TYPE.NE.'2'.AND.INF.CPL_TYPE.NE.'9') THEN
	    TYPE*,IAM(),'Invalid competitor count or record type...'
	    GOTO 9999	
	ENDIF
C
C Set pool file name.
C
        WRITE (CDCPPFN,904) GIND,DRAW
        DCPPFN(1) = FILEPACK
        IF(UPDATE) WRITE(6,905) IAM(),CDCPPFN
C
	DCPDRW = DRAW
	DCPSTS = GAMOPN
C
C Calculate new checksum
C
	BUFIDX = 1
	CALL MOVBYT(DCPENM,1,BYTTAB,BUFIDX,CPLENM_LEN)
	BUFIDX = BUFIDX + CPLENM_LEN
	CALL MOVBYT(DCPDES,1,BYTTAB,BUFIDX,CPLDES_LEN)
	BUFIDX = BUFIDX + CPLDES_LEN
	DO I=1,MAXCPLRW
	  CALL MOVBYT(DCPNMS(1,I),1,BYTTAB,BUFIDX,CPLNMS_LEN)
	  BUFIDX = BUFIDX + CPLNMS_LEN
	ENDDO
	BUFIDX = BUFIDX - 1

	CALL CHECKSUM(BYTTAB,1,BUFIDX,REV4)
	CALL ILBYTE(REV1,DCPREV,0)	    
        IF(DCPDRW.EQ.M251-1) THEN
	   REV1 = MOD(REV1+DCPDRW,(M251-10))+ 1
        ELSE
	   REV1 = MOD(REV1+DCPDRW,M251)+ 1
        ENDIF
	REV2 = MOD(DCPDRW,255)
	CALL ILBYTE(REV3,DCPREV,2)          !GET PREVIOUS TEXT REV #
	REV3 = MOD(PREV3 + REV3,255) + 1
	CALL ISBYTE(REV1,DCPREV,0)          !CONTROL REV BYTE (SEQUENCE#)
	CALL ISBYTE(REV2,DCPREV,1)          !DRAW REV BYTE
	CALL ISBYTE(REV3,DCPREV,2)          !TEXT REV # BYTE  (SEQUENCE#)
	CALL ISBYTE(REV4,DCPREV,3)          !TEXT CHECKSUM BYTE
C
C Verify all data.
C
        IF(DCPBSD.GT.DCPESD) THEN
           TYPE*,IAM(),'Begining sales date greater then ending sales date'
	   GOTO 9999
        ENDIF
        IF(DCPBSD.EQ.0) THEN
           TYPE*,IAM(),'Begining sales date not set '
           GOTO 9999
        ENDIF
        IF(DCPDAT.LT.DCPESD) THEN
           TYPE*,IAM(),'Event date is before last sales date'
           GOTO 9999
        ENDIF
        IF(DCPESD.EQ.0) THEN
           TYPE*,IAM(),'Ending sales date not set'
           GOTO 9999
        ENDIF
        IF(DCPPRC.EQ.0) THEN
           TYPE*,IAM(),'Base price not set'
           GOTO 9999
        ENDIF
        IF(DCPSPR.EQ.0) THEN
           TYPE*,IAM(),'Pool percentage not set'
           GOTO 9999
        ENDIF
C
        DO I=DCPBSD,DCPESD
          CALL READW(DFDB,I,DAFREC,ST)
          IF(ST.NE.0) CALL FILERR(SCFSFN(1,DAF),2,ST,I)
          I2DATE(5)=I
          CALL LCDATE(I2DATE)
          IF(DAFSTS.GT.DSOPEN) THEN
            WRITE(6,907) IAM(),(I2DATE(K),K=7,13)
            GOTO 9999
          ENDIF
          IF(DAFDRW(GNUM).NE.0.AND.DAFDRW(GNUM).NE.DRAW) THEN
            WRITE(6,906) IAM(),(I2DATE(K),K=7,13),GIND,DAFDRW(GNUM)
	    IF(UPDATE) THEN
              CALL INPYESNO('Do you want to overwrite [Y/N] ?',FLAG)
              IF(FLAG.NE.1) THEN
	       SKIP = .TRUE.
	       GOTO 1000
	      ENDIF
	    ENDIF
          ENDIF
	ENDDO
C
	IF(UPDATE) THEN
          DO I=DCPBSD,DCPESD
           CALL READW(DFDB,I,DAFREC,ST) 
	   IF(ST.NE.0)CALL FILERR(SCFSFN(1,DAF),2,ST,I)
           DAFDRW(GNUM)=DRAW
           CALL WRITEW(DFDB,I,DAFREC,ST)
           IF(ST.NE.0) CALL FILERR(SCFSFN(1,DAF),3,ST,I)
	   WRITE(6,910) IAM(),(SCFSFN(K,DAF),K=1,5),I
	  ENDDO
C
	  CALL WRITEW(FDB,DRAW,DCPREC,ST)
	  IF(ST.NE.0) CALL FILERR(SCFGFN(1,GNUM),3,ST,DRAW)
	  WRITE(6,911) IAM(),(SCFGFN(K,GNUM),K=1,5),DRAW
	  CALL WRITEW(VFDB,DRAW,DCPREC,ST)
	  IF(ST.NE.0) CALL FILERR(SCFGVN(1,GNUM),3,ST,DRAW)
	  WRITE(6,911) IAM(),(SCFGVN(K,GNUM),K=1,5),DRAW
C
	  WRITE(6,908) IAM(),GTNAMES(TCPL),GIND,DRAW
	ENDIF
C
C Generating report...
C
	IF(FIRST) THEN
          FIRST = .FALSE.
	  WRITE(FILNAM,919) 
	  REPLUN = 7
	  CALL ROPEN(FILNAM,REPLUN,ST)
          IF(ST.NE.0) THEN
            TYPE*,IAM(),'Error opening ',FILNAM,' status=',ST
	    GOTO 9999
          ENDIF
	  PAGE = 0
	  WRITE(TITL_NAME,920) HDR_WEEK,HDR_YEAR 
	  CALL TITLE(TITL_NAME,FILNAM,1,REPLUN,PAGE,DAYCDC)
	  WRITE(REPLUN,925)
	  WRITE(REPLUN,921) HDR_DATE(8:9),HDR_DATE(6:7),HDR_DATE(2:5),
     *        HDR_TIME(2:3),HDR_TIME(4:5),HDR_TIME(6:7)
	  WRITE(REPLUN,922)
	ENDIF
	WRITE(REPLUN,923)
	WRITE(REPLUN,924)
	I2DATE(VCDC) = DCPBSD
	CALL LCDATE(I2DATE)
	I2DATE2(VCDC) = DCPESD
	CALL LCDATE(I2DATE2)
	I2DATE3(VCDC) = DCPDAT
	CALL LCDATE(I2DATE3)
	HRS=DCPTIM/3600
	MINS=(DCPTIM-HRS*3600)/60
	WRITE(REPLUN,926) GIND,DCPDRW,(' ',K=1,4),
     *       (I2DATE(K),K=9,13),(I2DATE2(K),K=9,13),HRS,MINS,
     *       (I2DATE3(K),K=9,13),' ',
     *       DCPPRC*DYN_BETUNIT/DOLL_BASE,
     *       MOD(DCPPRC*DYN_BETUNIT,DOLL_BASE)

	WRITE(REPLUN,922)
	HRS=DCPEVT(1)/3600
	MINS=(DCPEVT(1)-HRS*3600)/60
	I2DATE2(VCDC) = DCPEVD(1)
	CALL LCDATE(I2DATE2)
	WRITE(REPLUN,929) (DCPENM(K,1),K=1,4),
     *       (' ',K=9,13),(' ',K=9,13),HRS,MINS,
     *       (I2DATE2(K),K=9,13),TV_CHANNEL(1)
	DO I = 1, MAXCPLRW/4
	  IF(DCPSTA(I).NE.GAMOPN) GOTO 500
	  IF(DCPSTA(I+MAXCPLRW/4).NE.GAMOPN) THEN
	     WRITE(REPLUN,928) I,(DCPNMS(K,I),K=1,4)
	   ELSE
	     WRITE(REPLUN,927) I,(DCPNMS(K,I),K=1,4),
     *                         I+MAXCPLRW/4,(DCPNMS(K,I+MAXCPLRW/4),K=1,4)
	   ENDIF
	ENDDO
500	CONTINUE	
	WRITE(REPLUN,922)
	HRS=DCPEVT(2)/3600
	MINS=(DCPEVT(2)-HRS*3600)/60
	I2DATE2(VCDC) = DCPEVD(2)
	CALL LCDATE(I2DATE2)
	WRITE(REPLUN,929) (DCPENM(K,2),K=1,4),
     *       (' ',K=9,13),(' ',K=9,13),HRS,MINS,
     *       (I2DATE2(K),K=9,13),TV_CHANNEL(2)
	WRITE(REPLUN,922)
	DO I = MAXCPLRW/2+1,MAXCPLRW/2+MAXCPLRW/4
	  IF(DCPSTA(I).NE.GAMOPN) GOTO 510
	  IF(DCPSTA(I+MAXCPLRW/4).NE.GAMOPN) THEN
	     WRITE(REPLUN,928) I-MAXCPLRW/2,(DCPNMS(K,I),K=1,4)
	   ELSE
	     WRITE(REPLUN,927) I-MAXCPLRW/2,(DCPNMS(K,I),K=1,4),
     *                         I-MAXCPLRW/4,(DCPNMS(K,I+MAXCPLRW/4),K=1,4)
	   ENDIF
	ENDDO
510	CONTINUE	
	WRITE(REPLUN,922)
C
C Close game files and DAF
