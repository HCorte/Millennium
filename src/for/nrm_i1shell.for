C ***** I1SHELL
C
C THIS WILL PERFORM A SHELL SORT OF A CHARACTERS IN AN I*4 ARRAY
C
C This is the same as I4SHELL except it sorts on a byte basis rather than
C on an Integer*4 basis.  This is necessary on DEC because of the byte
C ordering problems.
C
C CALLING SEQUENCE:
C     CALL I1SHELL(SORT,CNT,I4ARAY,INTEGER*4)
C INPUT
C     SORT      - SORT LIST OF POINTERS.
C     CNT       - NUMBER OF ITEMS TO SORT
C     I4ARAY    - ARRAY OF BYTE VALUES IN INTEGER*4 ARRAY TO SORT
C     INTEGER*4 - FIRST DIMENSION OF I*4 ARRAY (WILL BE MULTIPLIED BY 4 BY
C                 THIS ROUTINE)
C OUTPUT
C     SORT      - SORTED LIST OF POINTERS.
C     INTEGER*4 - SORTED IN ASCENDING ORDER BY BYTE VALUES
C
C
C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE I1SHELL(SORT,CNT,I1ARAY,LEN)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
C
	INTEGER*4 TEMP, XX, ZZ, L, I, K, J, M, LEN, CNT
	INTEGER*4 SORT(*)
	BYTE      I1ARAY(LEN*4,*)
C
	INTEGER*4 I1TEMP
C
C
	M=CNT
C
20	CONTINUE
	M=M/2
	IF(M.EQ.0) GO TO 8000
	J=1
	K=CNT-M
C
140	CONTINUE
	I=J
C
150	CONTINUE
	L=I+M
	DO 160 ZZ=1,LEN*4
	  IF(I1ARAY(ZZ,I) .LT. I1ARAY(ZZ,L))GO TO 210
	  IF(I1ARAY(ZZ,I) .GT. I1ARAY(ZZ,L))GO TO 170
160	CONTINUE
	GO TO 210
C
170	CONTINUE
	DO 175 XX=ZZ,LEN*4
	  I1TEMP=I1ARAY(XX,I)
	  I1ARAY(XX,I)=I1ARAY(XX,L)
	  I1ARAY(XX,L)=I1TEMP
175	CONTINUE
	TEMP=SORT(I)
	SORT(I)=SORT(L)
	SORT(L)=TEMP
	I=I-M
	IF(I.GE.1)GO TO 150
C
210	CONTINUE
	J=J+1
	IF(J.GT.K)GO TO 20
	GO TO 140
C
C
8000	CONTINUE
	RETURN
	END
