C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE DCGTB(TRABUF,OUTTAB,CRSBUF)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
	INCLUDE 'INCLIB:DESTRA.DEF'
	INCLUDE 'INCLIB:CRSBUF.DEF'
C
        INTEGER*4 IND, LENGTH, I
	BYTE      OUTTAB(*)
C
	INTEGER*4   I4TEMP
	INTEGER*2   I2TEMP(2)
	BYTE	    I1TEMP(4)
	EQUIVALENCE (I4TEMP,I2TEMP,I1TEMP)

C
C GET LENGTH
C
	IND=2
	I4TEMP=0
	I1TEMP(1)=OUTTAB(IND+0)
	I1TEMP(2)=OUTTAB(IND+1)
	LENGTH=I4TEMP
C
C GET ERROR CODE
C
	IND=12
	I4TEMP=0
	I1TEMP(1)=OUTTAB(IND+0)
	I1TEMP(2)=OUTTAB(IND+1)
	TRABUF(TIERR)=I4TEMP
	IND=IND+2

C
C
C
	IF(TRABUF(TIERR).GT.INOER) GOTO 8000
        IF(TRABUF(TIGMC).EQ.1.AND.LENGTH.NE.174) THEN
          TRABUF(TIERR)=INLTH
          GOTO 8000
        ENDIF
C
        IF(TRABUF(TIGMC).EQ.2.AND.LENGTH.NE.98) THEN
          TRABUF(TIERR)=INLTH
          GOTO 8000
        ENDIF
C
C CLASS NUMBER
C
	IND=IND+2
C
C GET CLASS ONE INFORMATION
C
	IF(TRABUF(TIGMC).EQ.1) THEN
	  DO 100 I=0,MAXGTA-1
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     I1TEMP(3)=OUTTAB(IND+2)
	     I1TEMP(4)=OUTTAB(IND+3)
	     CRSBUF(GTBGAM+I)=I4TEMP
	     IND=IND+2
100	  CONTINUE
	ELSE
C
C GET GAME TABLE INFORMATION
C
	  DO 110 I=0,MAXTBL-1
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBGNM+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE ONE INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ1+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE TWO INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ2+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE THREE INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ3+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE FOUR INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ4+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE FIVE INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ5+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE SIX INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ6+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE SEVEN INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ7+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE EIGHT INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ8+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE NINE INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ9+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE TEN INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ10+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE ELEVEN INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ11+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE TWELVE INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ12+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE THIRTEEN INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ13+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE FOURTEEN INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ14+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE FIFTEEN INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ15+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE SIXTEEN INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ16+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE SEVENTEEN INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ17+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE EIGHTEEN INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ18+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE NINETEEN INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ19+I)=I4TEMP
	     IND=IND+2
C
C GET PRIZE TWENTY INFO
C
	     I4TEMP=0
	     I1TEMP(1)=OUTTAB(IND+0)
	     I1TEMP(2)=OUTTAB(IND+1)
	     CRSBUF(GTBPRZ20+I)=I4TEMP
	     IND=IND+2
C
110	  CONTINUE
	ENDIF
C
8000	CONTINUE
	RETURN
	END
