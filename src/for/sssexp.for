C
C SUBROUTINE SSSEXP
C $Log:   GXAFXT:[GOLS]SSSEXP.FOV  $
C  
C
C
C SUBROUTINE TO EXPAND SUPERSCORE SYSTEM BETS
C
C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE SSSEXP(TRABUF,BETS,COUNT)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:DESTRA.DEF'

	INTEGER*4 BETS(2,3,16000)
	INTEGER*4 COUNT
	INTEGER*4 OFF1,OFF2,OFF3,OFF4,OFF5,OFF6
	INTEGER*4 I1,I2,I3,I4,I5,I6
C
C
	IF(TRABUF(TWSYST).EQ.NOSYS) THEN
	  COUNT=1
	  BETS(1,1,1)=TRABUF(TWSSBET)
	  BETS(2,1,1)=TRABUF(TWSSBET+1)
	  BETS(1,2,1)=TRABUF(TWSSBET+2)
	  BETS(2,2,1)=TRABUF(TWSSBET+3)
	  BETS(1,3,1)=TRABUF(TWSSBET+4)
	  BETS(2,3,1)=TRABUF(TWSSBET+5)
10	  CONTINUE
	  RETURN
	ENDIF
C
C EXPAND SYSTEM BETS
C
	COUNT=0

        IF(TRABUF(TWSSHM2).NE.0.AND.TRABUF(TWSSHM3).NE.0) THEN

           OFF1=-1
           OFF2=OFF1+TRABUF(TWSSHM1)
           OFF3=OFF2+TRABUF(TWSSAW1)
           OFF4=OFF3+TRABUF(TWSSHM2)
           OFF5=OFF4+TRABUF(TWSSAW2)
           OFF6=OFF5+TRABUF(TWSSHM3)

           DO 1060 I1=1,TRABUF(TWSSHM1)
           DO 1050 I2=1,TRABUF(TWSSAW1)
           DO 1040 I3=1,TRABUF(TWSSHM2)
           DO 1030 I4=1,TRABUF(TWSSAW2)
           DO 1020 I5=1,TRABUF(TWSSHM3)
           DO 1010 I6=1,TRABUF(TWSSAW3)

	      COUNT=COUNT+1
	      BETS(1,1,COUNT)=TRABUF(TWSSBET+OFF1+I1)
	      BETS(2,1,COUNT)=TRABUF(TWSSBET+OFF2+I2)
	      BETS(1,2,COUNT)=TRABUF(TWSSBET+OFF3+I3)
	      BETS(2,2,COUNT)=TRABUF(TWSSBET+OFF4+I4)
	      BETS(1,3,COUNT)=TRABUF(TWSSBET+OFF5+I5)
	      BETS(2,3,COUNT)=TRABUF(TWSSBET+OFF6+I6)

1010       CONTINUE
1020       CONTINUE
1030       CONTINUE
1040       CONTINUE
1050       CONTINUE
1060       CONTINUE

        ELSEIF(TRABUF(TWSSHM2).NE.0.AND.TRABUF(TWSSHM3).EQ.0) THEN

           OFF1=-1
           OFF2=OFF1+TRABUF(TWSSHM1)
           OFF3=OFF2+TRABUF(TWSSAW1)
           OFF4=OFF3+TRABUF(TWSSHM2)

           DO 2060 I1=1,TRABUF(TWSSHM1)
           DO 2050 I2=1,TRABUF(TWSSAW1)
           DO 2040 I3=1,TRABUF(TWSSHM2)
           DO 2030 I4=1,TRABUF(TWSSAW2)

	      COUNT=COUNT+1
	      BETS(1,1,COUNT)=TRABUF(TWSSBET+OFF1+I1)
	      BETS(2,1,COUNT)=TRABUF(TWSSBET+OFF2+I2)
	      BETS(1,2,COUNT)=TRABUF(TWSSBET+OFF3+I3)
	      BETS(2,2,COUNT)=TRABUF(TWSSBET+OFF4+I4)

2030       CONTINUE
2040       CONTINUE
2050       CONTINUE
2060       CONTINUE

        ELSEIF(TRABUF(TWSSHM2).EQ.0.AND.TRABUF(TWSSHM3).EQ.0) THEN

           OFF1=-1
           OFF2=OFF1+TRABUF(TWSSHM1)

           DO 3060 I1=1,TRABUF(TWSSHM1)
           DO 3050 I2=1,TRABUF(TWSSAW1)

	      COUNT=COUNT+1
	      BETS(1,1,COUNT)=TRABUF(TWSSBET+OFF1+I1)
	      BETS(2,1,COUNT)=TRABUF(TWSSBET+OFF2+I2)

3050       CONTINUE
3060       CONTINUE

        ENDIF
	RETURN
	END

