C SUBROUTINE MWIN_WINLOD
C
C SUBROUTINE TO LOAD VLF/TCF RECORDS TO DRAW FILES
C
C
C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE MWIN_WINLOD(FUN,RECORD)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
	INCLUDE 'INCLIB:DESLOG.DEF'
	INCLUDE 'INCLIB:PRMLOG.DEF'
C
        INTEGER*4 NAMVLW(5),NAMTCW(5)                              !V01
	INTEGER*4 K, TBLOCK, VBLOCK, TIND, VIND, ST, TCFCNT, VLFCNT
	INTEGER*4 VLFBUF(72,113),TCFBUF(48,170),VFDB(7),TFDB(7)
	INTEGER*4 VLFREC(8192),TCFREC(8192),RECORD(*),FUN
	EQUIVALENCE(VLFREC(1),VLFBUF(1,1))
	EQUIVALENCE(TCFREC(1),TCFBUF(1,1))
	DATA VLFREC/8192*0/,TCFREC/8192*0/
	DATA VLFCNT/0/,TCFCNT/0/
C
C
	IF(FUN.EQ.1) GOTO 100      !OPEN FILES
	IF(FUN.EQ.2) GOTO 200      !WRITE VLF RECORD
	IF(FUN.EQ.3) GOTO 300      !WRITE TCF RECORD
	IF(FUN.EQ.4) GOTO 400      !CLOSE FILES
C
C OPEN FILES
C
100	CONTINUE

        CALL WINGETW('MWINTSK ',NAMVLW,NAMTCW)                    !V01
	CALL OPENW(VLW,NAMVLW,4,0,0,ST)
	CALL IOINIT(VFDB,VLW,128*256)
	IF(ST.NE.0) CALL FILERR(NAMVLW,1,ST,0)
	VIND=1
	VBLOCK=0
C
CCC     CALL OPENW(TCW,NAMTCW,4,0,0,ST)
CCC     CALL IOINIT(TFDB,TCW,128*256)
CCC     IF(ST.NE.0) CALL FILERR(NAMTCW,1,ST,0)
CCC	TIND=1
CCC	TBLOCK=0

	RETURN
C
C WRITE VLF RECORD
C
200	CONTINUE
	VLFCNT=VLFCNT+1
	CALL FASTMOV(RECORD,VLFBUF(1,VIND),72)
	VIND=VIND+1
	IF(VIND.GT.113) THEN
	  VBLOCK=VBLOCK+1
	  CALL WRITEW(VFDB,VBLOCK,VLFREC,ST)
	  IF(ST.NE.0) CALL FILERR(NAMVLW,3,ST,VBLOCK)
	  CALL FASTSET(0,VLFREC,8192)
	  VIND=1
	ENDIF
	RETURN
C
C WRITE RECORD TO CARRYOVER FILE
C
300	CONTINUE
CCC	TCFCNT=TCFCNT+1
CCC	CALL FASTMOV(RECORD,TCFBUF(1,TIND),48)
CCC	TIND=TIND+1
CCC	IF(TIND.GT.170) THEN
CCC	  TBLOCK=TBLOCK+1
CCC	  CALL WRITEW(TFDB,TBLOCK,TCFREC,ST)
CCC	  IF(ST.NE.0) CALL FILERR(NAMTCW,3,ST,TBLOCK)
CCC	  CALL FASTSET(0,TCFREC,8192)
CCC	  TIND=1
CCC	ENDIF
	RETURN
C
C CLOSE FILES
C
400	CONTINUE
	VBLOCK=VBLOCK+1
	CALL WRITEW(VFDB,VBLOCK,VLFREC,ST)
	IF(ST.NE.0) CALL FILERR(NAMVLW,3,ST,VBLOCK)
	CALL CLOSEFIL(VFDB)
C
C
CCC	TBLOCK=TBLOCK+1
CCC	CALL WRITEW(TFDB,TBLOCK,TCFREC,ST)
CCC	IF(ST.NE.0) CALL FILERR(NAMTCW,3,ST,TBLOCK)
CCC	CALL CLOSEFIL(TFDB)
C
C
	WRITE(5,900) IAM(),VLFCNT,(NAMVLW(K),K=1,5)
CCC	WRITE(5,900) IAM(),TCFCNT,(NAMTCW(K),K=1,5)
	RETURN
C
C
900	FORMAT(1X,A,1X,I8,' records loaded to ',5A4)
	END
