C
C Output parameters:
C
C	BYTE		OUTBUF(*)    OUTPUT MESSAGE
C	INTEGER*4	MES_LEN	     MESSAGE LENGTH
C	INTEGER*4	RET_CODE:
C		0		-  no error, message accepted;
C		value >= 11	-  error number to be sent to Client.
C
C
C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE GUI_020(OUTBUF,MES_LEN,RET_CODE)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'

	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
        INCLUDE 'INCLIB:AGTINF.DEF'
        INCLUDE 'INCLIB:PRMAGT.DEF'
        INCLUDE 'INCLIB:RECDAF.DEF'
	INCLUDE 'INCLIB:DATBUF.DEF'
	INCLUDE 'INCLIB:GTNAMES.DEF'
	INCLUDE 'INCLIB:GUIMPRM.DEF'
	INCLUDE 'INCLIB:GUIARGS.DEF'
	INCLUDE 'INCLIB:GUIFIL.DEF'
C
C
	BYTE		OUTBUF(*)
	INTEGER*4	MES_LEN,RET_CODE
C
	INTEGER*4 NUM_COLS,NUM_ROWS
	INTEGER*4 GTYP, GIND
	INTEGER*4 DAT
C
	INTEGER*4  SALEN
 	PARAMETER (SALEN=NUMTOT*(NUMFIN+1)*MAXTYP)
C
	INTEGER*4  RSALEN
 	PARAMETER (RSALEN=NUMTOT*(NUMFIN+1)*2)
C
        INTEGER*4  TLEN
	PARAMETER (TLEN=NUMTOT*(NUMFIN+1))
C
        INTEGER*4  GNUM
        INTEGER*4  REC
        INTEGER*4  ST
	INTEGER*4  TEMP
	INTEGER*4  SALES(NUMTOT,NUMFIN+1,MAXTYP)
	INTEGER*4  ACT_GTYP(MAXTYP)/MAXTYP*0/
C
C  GET CDC
C
	CALL GUI_GETPARAMS(OUTBUF,ST)
	IF(ST.NE.0) THEN
	    RET_CODE = 11
	    RETURN
	ENDIF
	
	DAT = GUI_ARGVAL(1)
C
	IF(DAT.LE.0) DAT=DAYCDC
C
	IF(DAT.EQ.DAYCDC) THEN
	  CALL FASTMOV(DAYSTS,DAFSTS,DAFLEN)
	  GOTO 10
	ENDIF
C
	REC=DAT
C
	CALL READW(DAFFDB,REC,DAFREC,ST)
	IF(ST.NE.0) THEN
	  CALL OPS('Failed to read DAF file',ST,REC)
	  RET_CODE = 11
	  RETURN
	ENDIF
C
	IF(DAFSTS.EQ.DUNUSD) THEN
	  RET_CODE = 11
	  RETURN
	ENDIF
C
	IF(DAFSTS.EQ.DNOSAL) THEN
	  RET_CODE = 11
	  RETURN
	ENDIF
C
10	CONTINUE
	CALL FASTSET(0,SALES,SALEN)
C
	DO GTYP=1,MAXTYP
	DO GIND=1,MAXIND
	  GNUM=GTNTAB(GTYP,GIND)
	  IF(GNUM.NE.0) THEN
	     ACT_GTYP(GTYP) = 1
	     SALES(TRACNT,TWAG,GTYP) = SALES(TRACNT,TWAG,GTYP)+
     *	                            DAFTYP(TRACNT,TWAG,GNUM)
	     SALES(DOLAMT,TWAG,GTYP) = SALES(DOLAMT,TWAG,GTYP)+
     *	                            DAFTYP(DOLAMT,TWAG,GNUM)
C
	     SALES(TRACNT,TCAN,GTYP) = SALES(TRACNT,TCAN,GTYP)+
     *	       DAFTYP(TRACNT,TCAN,GNUM)+DAFTYP(TRACNT,TINC,GNUM)
	     SALES(DOLAMT,TCAN,GTYP) = SALES(DOLAMT,TCAN,GTYP)+
     *	       DAFTYP(DOLAMT,TCAN,GNUM)+DAFTYP(DOLAMT,TINC,GNUM)
C
	     SALES(TRACNT,TVAL,GTYP) = SALES(TRACNT,TVAL,GTYP)+
     *         DAFTYP(TRACNT,TVAL,GNUM)+DAFTYP(TRACNT,TREF,GNUM)+
     *	       DAFDIS(TRACNT,GNUM)
	     SALES(DOLAMT,TVAL,GTYP) = SALES(DOLAMT,TVAL,GTYP)+
     *         DAFTYP(DOLAMT,TVAL,GNUM)+DAFTYP(DOLAMT,TREF,GNUM)+
     *         DAFDIS(DOLAMT,GNUM)
C
	     SALES(TRACNT,TRET,GTYP) = SALES(TRACNT,TRET,GTYP)+
     *	                            DAFTYP(TRACNT,TRET,GNUM)
	     SALES(DOLAMT,TRET,GTYP) = SALES(DOLAMT,TRET,GTYP)+
     *	                            DAFTYP(DOLAMT,TRET,GNUM)
	  ENDIF
	ENDDO
	ENDDO
C
C Build GUI message
C
	CALL GUIARG_INIT()
C
	NUM_COLS = 9
	NUM_ROWS = 1
	CALL GUIARG_NEXT_SET(OUTBUF,NUM_COLS)
C
	DO 20 GTYP=1,MAXTYP
	  IF(ACT_GTYP(GTYP).NE.1) GOTO 20
	  TEMP=GTYP
	  CALL GUIARG_BYTE(OUTBUF,TEMP)
C
	  TEMP = SALES(TRACNT,TWAG,GTYP)
	  CALL GUIARG_INT4(OUTBUF,TEMP)
C
	  TEMP = SALES(DOLAMT,TWAG,GTYP)
	  CALL GUIARG_MONY(OUTBUF,TEMP)
C
	  TEMP = SALES(TRACNT,TCAN,GTYP)
	  CALL GUIARG_INT4(OUTBUF,TEMP)
C
	  TEMP = SALES(DOLAMT,TCAN,GTYP)
	  CALL GUIARG_MONY(OUTBUF,TEMP)
C
	  TEMP = SALES(TRACNT,TVAL,GTYP)
	  CALL GUIARG_INT4(OUTBUF,TEMP)
C
	  TEMP = SALES(DOLAMT,TVAL,GTYP)
	  CALL GUIARG_MONY(OUTBUF,TEMP)
C
	  TEMP = SALES(TRACNT,TRET,GTYP)
	  CALL GUIARG_INT4(OUTBUF,TEMP)
C
	  TEMP = SALES(DOLAMT,TRET,GTYP)
	  CALL GUIARG_MONY(OUTBUF,TEMP)
20	CONTINUE
C
	CALL GUIARG_SET_MESLEN(MES_LEN)
C
	RETURN
C
	END
