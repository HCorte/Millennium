C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE CRVAL(OUTTAB,TRABUF,BUF)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
	INCLUDE 'INCLIB:DESTRA.DEF'
C
        INTEGER*4 IND, BUF, CROSS, TIME(3)
	BYTE      OUTTAB(*)
	INTEGER*4 V_AGT, C_AGT
C
	INTEGER*4   I4TEMP, I
	INTEGER*2   I2TEMP(2)
	BYTE	    I1TEMP(4)
	EQUIVALENCE (I4TEMP,I2TEMP,I1TEMP)
C
C SET LENGTH
C
D	TYPE *,IAM(),'IN THE CRVAL HANDLER'
	IND=2
!	I4TEMP=28+TRABUF(TIBCH)*20
	I4TEMP=28+22+TRABUF(TIBCH)*20 !V10
	OUTTAB(IND+0) = I1TEMP(1)
	OUTTAB(IND+1) = I1TEMP(2)
	IND=IND+2
C
C SET GTECH BUFFER NUMBER
C
	I4TEMP=BUF
	OUTTAB(IND+0) = I1TEMP(1)
	OUTTAB(IND+1) = I1TEMP(2)
	IND=IND+2
C
C SET GTECH CROSS REFERENCE NUMBER
C
	CALL GETXRF(CROSS)
	TRABUF(TIXRF)=CROSS
	I4TEMP=CROSS
	OUTTAB(IND+0) = I1TEMP(1)
	OUTTAB(IND+1) = I1TEMP(2)
	OUTTAB(IND+2) = I1TEMP(3)
	OUTTAB(IND+3) = I1TEMP(4)
	IND=IND+4
C
C SET TRANSACTION TYPE CODE
C
	I4TEMP=13
	OUTTAB(IND+0) = I1TEMP(1)
	OUTTAB(IND+1) = I1TEMP(2)
	IND=IND+2
C
C SET VALIDATION MODE TYPE !V10
C
	I4TEMP=TRABUF(TIVMT)
	OUTTAB(IND+0) = I1TEMP(1)
	OUTTAB(IND+1) = I1TEMP(2)
	IND=IND+2
C
C TICKET INDICATOR
C
	I4TEMP=TRABUF(TIIND)
	OUTTAB(IND+0) = I1TEMP(1)
	OUTTAB(IND+1) = I1TEMP(2)
	IND=IND+2
C
C NUMBER OF TICKETS IN BATCH
C
	I4TEMP=TRABUF(TIBCH)
	OUTTAB(IND+0) = I1TEMP(1)
	OUTTAB(IND+1) = I1TEMP(2)
	IND=IND+2
C
C RETAILER, WHO GETS CREDIT
C
	IF(TRABUF(TIVAGT).NE.0) THEN
	   C_AGT = TRABUF(TIVAGT)
	   V_AGT = TRABUF(TAGT)
	ELSE
	   V_AGT = TRABUF(TIVAGT)
	   C_AGT = TRABUF(TAGT)
	ENDIF
	I4TEMP = C_AGT
        OUTTAB(IND+0) = I1TEMP(1)
        OUTTAB(IND+1) = I1TEMP(2)
        OUTTAB(IND+2) = I1TEMP(3)
        OUTTAB(IND+3) = I1TEMP(4)
        IND=IND+4
C
C VALIDATION MODE
C
	I4TEMP=TRABUF(TIVALM)
	OUTTAB(IND+0) = I1TEMP(1)
	OUTTAB(IND+1) = I1TEMP(2)
	IND=IND+2
C
C VALIDATING RETAILER NUMBER
C
	I4TEMP = V_AGT
	OUTTAB(IND+0) = I1TEMP(1)
	OUTTAB(IND+1) = I1TEMP(2)
	OUTTAB(IND+2) = I1TEMP(3)
	OUTTAB(IND+3) = I1TEMP(4)
	IND=IND+4
C
C Envelope ID
C
        I4TEMP = TRABUF(TIVENV)
        OUTTAB(IND+0) = I1TEMP(1)
        OUTTAB(IND+1) = I1TEMP(2)
        OUTTAB(IND+2) = I1TEMP(3)
        OUTTAB(IND+3) = I1TEMP(4)
        IND=IND+4
C
C PLAYER ID TYPE !V10
C
        I4TEMP = TRABUF(TIPLIDTYP)
        OUTTAB(IND+0) = I1TEMP(1)
        OUTTAB(IND+1) = I1TEMP(2)
        IND=IND+2
C
C PLAYER ID !V10
C
        I4TEMP = TRABUF(TIPLCARD)
        OUTTAB(IND+0) = I1TEMP(1)
        OUTTAB(IND+1) = I1TEMP(2)
        OUTTAB(IND+2) = I1TEMP(3)
        OUTTAB(IND+3) = I1TEMP(4)
        IND=IND+4
C
C BANK BRANCH !V10
C
        I4TEMP = TRABUF(TINIBBB)
        OUTTAB(IND+0) = I1TEMP(1)
        OUTTAB(IND+1) = I1TEMP(2)
        IND=IND+2
C
C BANK OFFICE !V10
C
        I4TEMP = TRABUF(TINIBBO)
        OUTTAB(IND+0) = I1TEMP(1)
        OUTTAB(IND+1) = I1TEMP(2)
        IND=IND+2
C
C BANK ACCOUNT (PART 1) !V10
C
        I4TEMP = TRABUF(TINIBBA1)
        OUTTAB(IND+0) = I1TEMP(1)
        OUTTAB(IND+1) = I1TEMP(2)
        OUTTAB(IND+2) = I1TEMP(3)
        OUTTAB(IND+3) = I1TEMP(4)
        IND=IND+4
C
C BANK ACCOUNT (PART 2) !V10
C
        I4TEMP = TRABUF(TINIBBA2)
        OUTTAB(IND+0) = I1TEMP(1)
        OUTTAB(IND+1) = I1TEMP(2)
        OUTTAB(IND+2) = I1TEMP(3)
        OUTTAB(IND+3) = I1TEMP(4)
        IND=IND+4
C
C CHECK DIGITS !V10
C
        I4TEMP = TRABUF(TINIBCD)
        OUTTAB(IND+0) = I1TEMP(1)
        OUTTAB(IND+1) = I1TEMP(2)
        IND=IND+2
C
C SET GAME NUMBER
C
D	TYPE *,IAM(),'THE TRABUF(TIBCH) IS ',TRABUF(TIBCH)
	DO 100 I=0,TRABUF(TIBCH)-1
	   I4TEMP=TRABUF(TIGAM1+I)
	   OUTTAB(IND+0) = I1TEMP(1)
	   OUTTAB(IND+1) = I1TEMP(2)
	   IND=IND+2
C
C SET VALIDATION CDC
C
	   I4TEMP=TRABUF(TICDC1+I)
	   OUTTAB(IND+0) = I1TEMP(1)
	   OUTTAB(IND+1) = I1TEMP(2)
	   IND=IND+2
C
C SET PACK NUMBER
C
	   I4TEMP=TRABUF(TIPCK1+I)
	   OUTTAB(IND+0) = I1TEMP(1)
	   OUTTAB(IND+1) = I1TEMP(2)
	   OUTTAB(IND+2) = I1TEMP(3)
	   OUTTAB(IND+3) = I1TEMP(4)
	   IND=IND+4
C
C SET VIRN NUMBER (9 DIGITS)
C
	   I4TEMP=TRABUF(TIVRN1+I)
	   OUTTAB(IND+0) = I1TEMP(1)
	   OUTTAB(IND+1) = I1TEMP(2)
	   OUTTAB(IND+2) = I1TEMP(3)
	   OUTTAB(IND+3) = I1TEMP(4)
	   IND=IND+4
C
C SET VIRN CHECK DIGITS
C
	   I4TEMP=TRABUF(TILTX1+I)
	   OUTTAB(IND+0) = I1TEMP(1)
	   OUTTAB(IND+1) = I1TEMP(2)
	   IND=IND+2
C
C SET VALIDATION TIME
C
C V02 GAP
C	   
	   IF(TRABUF(TITIM1+I).EQ.0) THEN
		CALL ICLOCK(2,TIME)
		I4TEMP=TIME(1)
D	        TYPE *,IAM(),'USING THE GAP LOGIC, TIME IS:',I4TEMP
	   ELSE
		I4TEMP=TRABUF(TITIM1+I)
D	        TYPE *,IAM(),'USING THE NON-GAP LOGIC, TIME IS:',I4TEMP
	   ENDIF
C V02 GAP
	   OUTTAB(IND+0) = I1TEMP(1)
	   OUTTAB(IND+1) = I1TEMP(2)
	   OUTTAB(IND+2) = I1TEMP(3)
	   OUTTAB(IND+3) = I1TEMP(4)
	   IND=IND+4
100	CONTINUE
C
C
C
	RETURN
	END
