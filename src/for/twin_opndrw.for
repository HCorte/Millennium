C SUBROUTINE TO OPEN DRAW FILES FOR WINSEL.
C
C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE TWIN_OPNDRW(FILE,UNT)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INTEGER*4   FILE(5)
	INTEGER*4   UNT, ST
	INTEGER*4   INPLEN, I4FILE
	CHARACTER   CXFILE*4
	EQUIVALENCE (I4FILE,CXFILE)	
C
10	CONTINUE
	CALL USRCLOS1(     UNT)
	CALL OPENW(UNT,FILE,5,0,0,ST)
	IF(ST.EQ.0) RETURN
C
C
	WRITE(5,900) IAM(),FILE,ST
	CALL PRMTEXT('Enter new draw pack volume name: ',CXFILE,INPLEN)
	FILE(1) = I4FILE
	WRITE(5,920) IAM(),FILE(1)
	CALL GPAUSE
	GOTO 10
C
C
900	FORMAT(1X,A,1X,5A4,' open error> ',I4)
910	FORMAT(A4)
920	FORMAT(1X,A,1X,'Mount ',A4,' pack and continue TWINSEL')
	END
