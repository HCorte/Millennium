C
C SUBROUTINE LOAD_EUM1CF
C
C V01 11-MAR-2016 SCML CREATION (M16 PROJECT)
C
C SUBROUTINE TO LOAD EUM1 GAME CONFIGURATION FILE INTO MEMORY
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C
C Copyright 2016 SCML Corporation. All rights reserved.
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C
C=======OPTIONS /CHECK=NOOVERFLOW/EXT
        SUBROUTINE LOAD_EUM1CF
        IMPLICIT NONE
C
        INCLUDE 'INCLIB:SYSPARAM.DEF'
        INCLUDE 'INCLIB:SYSEXTRN.DEF'
        INCLUDE 'INCLIB:GLOBAL.DEF'
        INCLUDE 'INCLIB:EURCOM.DEF'
        INCLUDE 'INCLIB:RECEUM1CF.DEF'
C
        INTEGER*4  ST
C
        TYPE*,IAM(),' Reading EUM1 configuration file'
        CALL GETEUM1CONF(EUM1CF_REC, ST)
        IF(ST.NE.0) CALL GSTOP(GEXIT_FATAL)
C
        TYPE*,IAM(),' Loading EUM1 configuration file into memory'
C
        CALL FASTMOV(EUM1CF_LNGNAME, EGLNAMES(1,EUM1GN), 5)
        EGSNAMES(EUM1GN) = EUM1CF_SHGNAME
        CALL FASTMOV(EUM1CF_FILNAME, EGFNAMES(1,EUM1GN), 5)
C
        EUMGACTIVE(EUM1GI) = EUM1CF_GACTIVE
        EUMGBPRICE(EUM1GI) = EUM1CF_GBPRICE
        EUMGOPTSON(EUM1GI) = EUM1CF_GOPTSON
        EUMGOPTCTR(EUM1GI) = EUM1CF_GOPTCTR
        EUMJKACTIV(EUM1GI) = EUM1CF_JKACTIV
        EUMDBJKFRE(EUM1GI) = EUM1CF_DBJKFRE
        EUMNCURACT(EUM1GI) = EUM1CF_NCURACT
        EUMSMACTIV(EUM1GI) = EUM1CF_SMACTIV
        EUMPNIFREQ(EUM1GI) = EUM1CF_PNIFREQ
        EUMMAXBETS(EUM1GI) = EUM1CF_MAXBETS
        EUMMAXFRAC(EUM1GI) = EUM1CF_MAXFRAC
        CALL FASTMOV(EUM1CF_DBITMAP, EUMDBITMAP(1,EUM1GI), EMAXDBM)
        EUMTKTCHAR(EUM1GI) = EUM1CF_TKTCHAR
        EUMCTRLREV(EUM1GI) = EUM1CF_CTRLREV
        EUMTEXTREV(EUM1GI) = EUM1CF_TEXTREV
        EUMTTXTREV(EUM1GI) = EUM1CF_TTXTREV
        EUMSOMIMRB(EUM1GI) = EUM1CF_SOMIMRB                                     !SoM RAFFLES MINIMUM NUMBER HAVING INTERVAL MODE REPRESENTATION
C
        RETURN
        END
C
C END LOAD_EUM1CF.FOR
C
