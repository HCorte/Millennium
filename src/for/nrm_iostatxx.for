C *** IOSTATXX - WRITE WITHOUT A WAIT
C
C=======OPTIONS /CHECK=NOOVERFLOW
	SUBROUTINE IOSTATXX(RAB, FDB, STATUS)
	IMPLICIT NONE
C
	INCLUDE	'INCLIB:SYSPARAM.DEF'
	INCLUDE	'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:DISKIO.DEF'
	INCLUDE	'($SYSSRVNAM)'
	INCLUDE '($RABDEF)'
C
	RECORD	    /RABDEF/ RAB
	INTEGER*4   FDB(FDB_LENGTH)
	INTEGER*4   STATUS
C
C
C
C	WAIT FOR THE I/O TO COMPLETE
C
	STATUS = SYS$WAIT(RAB)
	IF(STATUS)THEN
	  STATUS = 0		    ! FOR COMPATIBILITY
	ENDIF
C
	FDB(FDB_STAT) = STATUS
C
C	turn asynchronous bit OFF
C
	RAB.RAB$L_ROP = RAB.RAB$L_ROP .AND. .NOT.RAB$M_ASY
C
	RETURN
	END
