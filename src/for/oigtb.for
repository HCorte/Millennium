	SUBROUTINE OIGTB(TRABUF,OUTTAB,OUTLEN,CRSBUF)
	IMPLICIT NONE
C
	INCLUDE 'INCLIB:SYSPARAM.DEF'
	INCLUDE 'INCLIB:SYSEXTRN.DEF'
	INCLUDE 'INCLIB:GLOBAL.DEF'
	INCLUDE 'INCLIB:CONCOM.DEF'
	INCLUDE 'INCLIB:DESTRA.DEF'
	INCLUDE 'INCLIB:CRSBUF.DEF'
	INCLUDE 'INCLIB:CHKSUMCM.DEF'
C
        INCLUDE 'INCLIB:INSNAM.DEF'               !Instant Game Names
C
        INTEGER*4 MYCHKSUM, CHKLEN, IND
        INTEGER*4 ERRTYP, I
	INTEGER*4 CHKDIG
C
	BYTE      OUTTAB(*)
	INTEGER*2 OUTLEN
C
	INTEGER*4   I4TEMP
	INTEGER*2   I2TEMP(2)
	BYTE	    I1TEMP(4)
	EQUIVALENCE (I4TEMP,I2TEMP,I1TEMP)
C
	DATA ERRTYP/Z90/

C
C CONTROL AND SEQUENCE NUMBER
C
	OUTTAB(1) = '20'X+TRABUF(TTRN)
C
C IF TRANSACTION STATUS IS NOT GOOD
C BUILD ERROR MESSAGE.
C
	IF(TRABUF(TSTAT).NE.GOOD.AND.TRABUF(TERR).NE.BCRS) THEN
	  OUTTAB(2) = ERRTYP
	  OUTTAB(5) = TRABUF(TERR)
	  OUTLEN=5
	  GOTO 1000
	ENDIF
C
C TYPE AND SUBTYPE
C
	OUTTAB(2) = 'DF'X
C
C TIME
C
	IND=5
	CALL HOST_TO_TERM(OUTTAB(IND),TRABUF(TTIM),3)
	IND=IND+3
C
C JULIAN DATE
C
	CALL HOST_TO_TERM(OUTTAB(IND),DAYJUL,2)
	IND=IND+2
C
C SERIAL NUMBER AND CHECK DIGITS
C
	CALL OUTGEN(TRABUF(TCDC),TRABUF(TSER),I4TEMP,CHKDIG)
	CALL HOST_TO_TERM(OUTTAB(IND),I4TEMP,3)
	OUTTAB(IND+3) = CHKDIG
	IND=IND+4
C
C INSTANT RESULT CODE ONE
C

	OUTTAB(IND+0) = 0
	IF(TRABUF(TIERR).NE.INOER) OUTTAB(IND+0) = 1
	IND=IND+1
C
C INSTANT RESULT CODE TWO
C
	OUTTAB(IND+0) = 0
	IF(TRABUF(TIERR).NE.INOER) OUTTAB(IND+0) = TRABUF(TIERR)
	IND=IND+1
C
C CLASS
C
	OUTTAB(IND+0) = TRABUF(TIGMC)
	IND=IND+1
C
C ACTIVE GAME NUMBERS
C
	IF(TRABUF(TIGMC).EQ.1) THEN
C
C PUT IN ACTIVE GAMES
C
	  DO 100 I=0,MAXGTA-1
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBGAM+I),2)
	     IND=IND+2
100	  CONTINUE
	ELSE
C
C PUT IN GAME NUMBER
C
	  DO 110 I=0,MAXTBL-1

             CALL HOST_TO_TERM(OUTTAB(IND), CRSBUF(GTBGNM+I), 2) 
	     IND=IND+2
C
C PUT IN PRIZE CODE ONE
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ1+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE TWO
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ2+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE THREE
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ3+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE FOUR
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ4+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE FIVE
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ5+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE SIX
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ6+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE SEVEN
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ7+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE EIGHT
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ8+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE NINE
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ9+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE TEN
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ10+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE ELEVEN
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ11+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE TWELVE
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ12+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE THIRTEEN
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ13+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE FOURTEEN
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ14+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE FIFTEEN
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ15+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE SIXTEEN
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ16+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE SEVENTEEN
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ17+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE EIGHTEEN
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ18+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE NINTEEN
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ19+I),2)
	     IND=IND+2
C
C PUT IN PRIZE CODE TWENTY
C
	     CALL HOST_TO_TERM(OUTTAB(IND),CRSBUF(GTBPRZ20+I),2)
	     IND=IND+2

110	  CONTINUE
	ENDIF
	OUTLEN=IND-1
C
C CALCULATE CHECKSUM
C
1000	CONTINUE
	I4CCITT = TRABUF(TCHK)
	CALL HOST_TO_TERM(OUTTAB(3),I4CCITT,2)
	CHKLEN=OUTLEN-1
	CALL GETCCITT(OUTTAB,1,CHKLEN,MYCHKSUM)
	I4CCITT = MYCHKSUM
	CALL HOST_TO_TERM(OUTTAB(3),I4CCITT,2)
C
 	RETURN
	END
