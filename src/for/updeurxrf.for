C SUBROUTINE UPDEURXRF
C
C UPDEURXRF.FOR
C
C V01 01-APR-2016 SCML M16 PROJECT
C
C SUBROUTINE TO UPDATE CROSS SYSTEM NUMBER OF EUROMILLIONS SYSTEM
C
C
C=======OPTIONS /CHECK=NOOVERFLOW
        SUBROUTINE UPDEURXRF(CROSS)
        IMPLICIT NONE
C
        INCLUDE 'INCLIB:SYSPARAM.DEF'
        INCLUDE 'INCLIB:SYSEXTRN.DEF'
        INCLUDE 'INCLIB:GLOBAL.DEF'
        INCLUDE 'INCLIB:EURCOM.DEF'
C
        INTEGER*4 CROSS, TMP_CROSS, TMP_NXTCROSS
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
        IF(CROSS.LT.0) RETURN
C
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C       SET CROSS REF NUMBER
CCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCCC
C
        TMP_CROSS = CROSS
100     CONTINUE
        IF(EXRFOFF.GT.0 .AND. TMP_CROSS .GT. EXRFOFF) THEN
          TMP_CROSS = TMP_CROSS - EXRFOFF
          GOTO 100
        ENDIF
C
        TMP_NXTCROSS = TMP_CROSS + 1
        IF(TMP_NXTCROSS .GT. EURS_NXTXRF) EURS_NXTXRF = TMP_NXTCROSS
C
        RETURN
C
        END
C
C  END UPDEURXRF.FOR
C
