C
C SUBROUTINE LOAD_RAF2CF
C
C V01 11-MAR-2016 SCML CREATION (M16 PROJECT)
C
C SUBROUTINE TO LOAD RAF2 GAME CONFIGURATION FILE INTO MEMORY
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C
C Copyright 2016 SCML Corporation. All rights reserved.
C+++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++++
C
C=======OPTIONS /CHECK=NOOVERFLOW/EXT
        SUBROUTINE LOAD_RAF2CF
        IMPLICIT NONE
C
        INCLUDE 'INCLIB:SYSPARAM.DEF'
        INCLUDE 'INCLIB:SYSEXTRN.DEF'
        INCLUDE 'INCLIB:GLOBAL.DEF'
        INCLUDE 'INCLIB:EURCOM.DEF'
        INCLUDE 'INCLIB:RECRAF2CF.DEF'
C
        INTEGER*4  ST
C
        TYPE*,IAM(),' Reading RAF2 configuration file'
        CALL GETRAF2CONF(RAF2CF_REC, ST)
        IF(ST.NE.0) CALL GSTOP(GEXIT_FATAL)
C
        TYPE*,IAM(),' Loading RAF2 configuration file into memory'
C
        CALL FASTMOV(RAF2CF_LNGNAME, EGLNAMES(1,RAF2GN), 5)
        EGSNAMES(RAF2GN) = RAF2CF_SHGNAME
        CALL FASTMOV(RAF2CF_FILNAME, EGFNAMES(1,RAF2GN), 5)
C
        RAFGACTIVE(RAF2GI) = RAF2CF_GACTIVE
        RAFGBPRICE(RAF2GI) = RAF2CF_GBPRICE
        RAFGOPTSON(RAF2GI) = RAF2CF_GOPTSON
        RAFGOPTCTR(RAF2GI) = RAF2CF_GOPTCTR
        RAFJKACTIV(RAF2GI) = RAF2CF_JKACTIV
        RAFDBJKFRE(RAF2GI) = RAF2CF_DBJKFRE
        RAFNCURACT(RAF2GI) = RAF2CF_NCURACT
        RAFSMACTIV(RAF2GI) = RAF2CF_SMACTIV
        RAFPNIFREQ(RAF2GI) = RAF2CF_PNIFREQ
        RAFMAXBETS(RAF2GI) = RAF2CF_MAXBETS
        RAFMAXFRAC(RAF2GI) = RAF2CF_MAXFRAC
        CALL FASTMOV(RAF2CF_DBITMAP, RAFDBITMAP(1,RAF2GI), EMAXDBM)
        RAFTKTCHAR(RAF2GI) = RAF2CF_TKTCHAR
        RAFCTRLREV(RAF2GI) = RAF2CF_CTRLREV
        RAFTEXTREV(RAF2GI) = RAF2CF_TEXTREV
        RAFTTXTREV(RAF2GI) = RAF2CF_TTXTREV
        RAFIMRBEGI(RAF2GI) = RAF2CF_IMRBEGI
C
        RETURN
        END
C
C END LOAD_RAF2CF.FOR
C
